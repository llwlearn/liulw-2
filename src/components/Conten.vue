            <h1 class="biao-ti">不可数名词</h1>
            <p>
                <span class="ding-yi">不可数名词没有复数的格式，只有单数的格式。</span><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有些人把不可数名词也变复数，这是错误的。比如有人犯这样的错误："A lot of
                foods"。"food"是不可数名词，不用
                加"s"变复数。直接说，"a lot of food"就对了。<span class="xia-hua-xian">不可数名词是单数概念</span>，所以作主语的时候，后面的谓语动词要考虑变单
                数格式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比如：water is important.不能说 water are important.
                具体情况以后详细讲。不过，有时候，我们需要用一个量
                词，表示不可数名词的数量。比如"water(水)"，我们可以说"一杯水，一瓶水"。英文就可以这样说："a glass of water. A
                bottle of water"，"A glass of "，"a bottle of "就相当于中文量词的作用。要表示"几杯水，几瓶水"的时候，要把"杯，
                瓶"变复数，但是"water"不能变复数。比如：two glasses of water(两杯水), three bottles of water (三瓶水)。<br />
                请看下面更多的例子：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;two cups of tea/ coffee 两杯茶/咖啡<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a bottle of milk /wine/ oil 一瓶牛奶/酒/油<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a kilogram of meat 一公斤肉<br /><br />
                <span class="xia-hua-xian">量词的构成方式有两种</span>：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.A/an/one+可数名词单数+of 比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a cup of 一杯 a bucket of 一桶<br /><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.数词+可数名词的复数+of 比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;two bottles of 两瓶 three tins of 三罐<br /><br />
                注意，<span class="xia-hua-xian">量词里面的"of"是没有意思的，不要跟后面的名词所有格里面的"of"混淆</span>。<br />
                另外，<span class="xia-hua-xian">量词其实也可以修饰可数名词的复数，但是不能修饰单数</span>。比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a basket of apples 一篮子苹果 a box of books 一箱书<br /><br />
                使用量词要注意：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.<span
                    class="xia-hua-xian">不是所有的可数名词，都能构成量词</span>。只有那种有内部空间或容器或者一些符合逻辑的可数名词才可以构成量词。<br />
                比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;①bag/basket/truck(有内部空间)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;②Cup/glass/bottle (容器)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;③Bar/piece/loaf (符合逻辑)<br /><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.<span
                    class="xia-hua-xian">大部分的量词都是修饰不可数名词</span>。而且不是每一个量词都能修饰每一个不可数名词，要注意逻辑问题。不能用中文
                的量词思维来判断英文的量词使用。<br /><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3.<span
                    class="xia-hua-xian">英文的量词一定不能修饰可数名词的单数</span>。<br /><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4.英文的量词虽然可以修饰可数名词的复数，但是不常见。而且要求非常严格，<span
                    class="xia-hua-xian">必须是可数的事物装到容器里面或者装
                    到有空间的事物里面，才能使用量词</span>。符合上面规则，很多时候，还是不行的，最重要的是要注意逻辑问题，而且是按英文
                的逻辑，不能按中文的量词思维。
            </p>
