            <h1 class="biao-ti" style="line-height: 50px">冠词</h1>
            <p>
                <span class="ding-yi">冠词是一种虚词，没有词义，没有数和格的变化，<br />
                    不能单独使用，只能帮助名词或起名词作用的其他词类说明其意义。</span><br />
                冠词分为定冠词"the (这,这些)"和不定冠词"a , an(一)"两大类。<br /><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"a"用在以辅音(指发音)开头的名词前面，读[ə]；"an"用在以元音开头的名词前，读[ən]。如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a book [buk] 一本书 &nbsp;&nbsp;&nbsp;&nbsp;a university 一所大学
                &nbsp;&nbsp;&nbsp;&nbsp;a pen 一支笔 &nbsp;&nbsp;&nbsp;&nbsp;a car 一辆车<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;an apple [ˈæpəl]一个苹果 &nbsp;&nbsp;&nbsp;&nbsp;an hour 一个小时
                &nbsp;&nbsp;&nbsp;&nbsp;an egg 一个鸡蛋<br />
                注意，这里说的辅音和元音，都是<span class="xia-hua-xian">指音标里面的发音</span>，不是指辅音字母和元音字母。<br />
                如果名词前面有一个形容词或者名词作定语，而这个形容词或者名词的发音也是元音开头，那么也要用"an"，如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;an <span class="xia-hua-xian">old</span> man 一个老人<br />
                (注意，"a old man"是错的，因为形容词 old 的发音是元音开头，必须用 an。这是初学者最常犯的冠词错误之一。)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;an <span class="xia-hua-xian">English</span> book 一本英语书 (不能说"a
                English book")<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;an <span class="xia-hua-xian">old</span> book 一本旧书 (不能说 "a old
                book")<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;定冠词"the"有两种发音，一个是读[ðə]，一个读[ði]。当后面的名词是辅音(指发音)开头的时候，读[ðə]，<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跟上面的内容一样，也是指发音，不指字母，如：the house,the book。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当后面的名词是元音(指发音)开头的时候，读[ði]。如：the apple,in the end。
            </p>
            <hr />
            <h2 class="biao-ti-1">不定冠词"a , an"的用法(一般表示"一"的意思)</h2>
            <h3 class="biao-ti-1">1,首次提到某人或某物</h3>
            <p>
                当第一次提到某人某物，并非特指，用不定冠词"a ,an"起介绍作用。如：
            </p>
            <ul>
                <li>He is <span class="xia-hua-xian">a</span> doctor. 他是医生。</li>
                <li>I am <span class="xia-hua-xian">a</span> teacher. 我是老师</li>
                <li>He is studying in <span class="xia-hua-xian">a</span> university.他在一所大学学习。</li>
                <li>I bought <span class="xia-hua-xian">a</span> book yesterday. 我昨天买了一本书。</li>
                <li><span class="xia-hua-xian">A</span> beautiful girl likes you.一个漂亮的女孩子喜欢你。</li>
                <li>She eats <span class="xia-hua-xian">an</span> egg every morning.她每天早上吃一个鸡蛋。</li>
            </ul>
            <p>
                注意：a,an 只能跟可数名词的单数，不能跟不可数名词，也不能跟可数名词的复数。<br />
                所以像"A news,A water, a people"这些都是错的。
            </p>
            <h3 class="biao-ti-1">2.在单数普通名词前</h3>
            <p>
                在单数普通名词前指一类人或者事物，不必翻译，如：
            </p>
            <ul>
                <li><span class="xia-hua-xian">An</span> ear is an organ for listening. 耳朵是听的器官</li>
                <li><span class="xia-hua-xian">A</span> fox is a cunning animal. 狐狸是狡猾的动物</li>
                <li><span class="xia-hua-xian">A</span> dog is a cute animal. 狗是可爱的动物。</li>
            </ul>
            <h3 class="biao-ti-1">3.用在表示时间或者度量单位的可数名词单数的前面，表示"每一"的意思。注意，必须是表
                示时间或度量单位的名词，其他的名词，一般不能表达这个意义。</h3>
            <p>表示时间的名词大概有：</p>
            <table style="margin-top: -2px">
                <tr>
                    <td>second 秒</td>
                    <td>minute 分钟</td>
                    <td>hour 小时</td>
                    <td>moming 早上</td>
                </tr>
                <tr>
                    <td>noon 中午</td>
                    <td>afternoon 下午</td>
                    <td>evening 傍晚</td>
                    <td>night 夜晚</td>
                </tr>
                <tr>
                    <td>day 天 /year 年</td>
                    <td>week 周</td>
                    <td>month 月</td>
                    <td>season 季度</td>
                </tr>
            </table>
            <p>
                表示度量单位的名词大概有：
            </p>
            <table>
                <tr>
                    <td>Centimeter 厘米</td>
                    <td>Meter 米</td>
                    <td>yard 码</td>
                    <td>Kilogram 公斤</td>
                </tr>
                <tr>
                    <td>kilometer 千米</td>
                    <td>Pound 磅</td>
                    <td>inch 英寸</td>
                    <td>Mile 英里</td>
                </tr>
            </table>
            <p>
                当 a,an 跟一个表示时间或度量单位的名词单数的时候，a,an 基本上等于 every 的用法。<br />
                所以：
            </p>
            <ul>
                <li>A day = every day 每天</li>
                <li>An hour=every hour 每小时</li>
                <li>A month=every month 每月</li>
            </ul>
            <p>
                但是，只有作时间状语，才能表达"每一"的意思：
            </p>
            <ul>
                <li>I teach English <span class="xia-hua-xian">a day</span>.=I teach English <span
                        class="xia-hua-xian">every day</span>.我每天教英语。</li>
                <li>I eat an apple <span class="xia-hua-xian">a day</span>.=I eat an apple <span
                        class="xia-hua-xian">every day</span>. 我每天吃一个苹果。</li>
                <li>He earns two thousand yuan <span class="xia-hua-xian">a month</span>.他每一个月赚两千块</li>
                <li>The train is running sixty miles <span class="xia-hua-xian">an hour</span>.火车每小时行驶 60 英里</li>
            </ul>
