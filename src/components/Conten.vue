            <h1 class="biao-ti">主语(非单三)+谓语+宾语</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般现在时态里面动作的句型，有两种构成方式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第一种是：当主语是第一人称单数和复数(I /we)，第二人称单数和复数(you)，第三人称复数(they)以及名词的复数的
                时候(可以简称为主语是"非第三人称单数"的情况)，<span class="xia-hua-xian">谓语动词使用原型</span>。主语和宾语不用发生变化。
            </p>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为了更方便学习造句，可以把"主语(非第三人称单数)+谓语+宾语"这个句型分成四个更具体详细的小句型：
            </p>
            <h3 class="biao-ti-1">1、人称代词主格(主语)+不及物动词(谓语)+状语</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型只有主语是人称代词"I/ we/ you/ they"的时候才属于非第三人称单数。
                所以这个句型还可以归纳为：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I/ &nbsp;&nbsp;we/
                &nbsp;&nbsp;you/ &nbsp;&nbsp;they +不及物动词原型+every day<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;只要从"I/ we/ you/ they"这四个人称代词主格中任意挑一个作主语，然后再从动词表里面随便挑一个<span
                    class="xia-hua-xian">不及物动词原型</span>
                作谓语，再加一个简单的时间状语"every day"，就可以构成一个最简单的<span class="xia-hua-xian">一般现在时态动作型</span>的句子，比如：
            </p>
            <ul>
                <li>1. I <span class="xia-hua-xian">sleep</span> every day 我每天睡觉</li>
                <li>2. We <span class="xia-hua-xian">work</span> every day 我们每天工作</li>
                <li>3. You <span class="xia-hua-xian">cry</span> every day</li>
                <li>4. They <span class="xia-hua-xian">walk</span> every day</li>
                <li>5. We <span class="xia-hua-xian">run</span> every day</li>
            </ul>
            <h3 class="biao-ti-1">2、名词复数(主语)+不及物动词原型(谓语)+状语</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型里面的主语最好是表示人的名词的复数，表示人的名词直接从名词表里面找。虽然名词的复数可以直接作主
                语，但是一般情况，还是需要加定冠词或限定词。如果不加定冠词或限定词，就是泛指，有时候可能不符合逻辑。比如：
            </p>
            <ul>
                <li>1. My friends <span class="xia-hua-xian">work</span> every day</li>
                <li>2. Nick’s classmates <span class="xia-hua-xian">run</span> every day</li>
                <li>3. The nurses <span class="xia-hua-xian">work</span> every day</li>
                <li>4. My daughters <span class="xia-hua-xian">cry</span> every day</li>
                <li>这个小句型的主语也可以是两个并列人名，作谓语的不及物动词也用原型：</li>
                <li style="list-style: none">1. Nick and Jack <span class="xia-hua-xian">work</span> every day</li>
                <li style="list-style: none">2. Lily and Lucy <span class="xia-hua-xian">cry</span> every day</li>
            </ul>
            <h3 class="biao-ti-1">3、人称代词主格(主语)+及物动词原型(谓语)+宾语+状语</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型的主语跟上面第 1 个句型一样，都是"I/ we/ you/
                they"。及物动词原型直接从动词表里面挑选。宾语可以
                是任何一个人称代词的宾格或者任何一个名词，当然作宾语的名词也要考虑可数名词单复数的问题。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为了避免造句的时候翻译中文，受中文影响，最好先从动词表里面挑选一个及物动词，然后根据及物动词意思，从名词
                表挑选一个合适的名词作宾语，或直接挑选一个合适的人称代词宾格作宾语。决定了及物动词和宾语之后，再从 I/ we/
                you/ they"中选一个符合逻辑的作主语。
            </p>
            <ul>
                <li>1. I <span class="xia-hua-xian">bake</span> potatoes every day </li>
                <li>2. We <span class="xia-hua-xian">bake</span> sweet potatoes every day</li>
                <li>3. you <span class="xia-hua-xian">buy</span> a book every year</li>
                <li>4. they <span class="xia-hua-xian">drink</span> water every day</li>
                <li>5. we <span class="xia-hua-xian">eat</span> rice every day</li>
                <li>6. we <span class="xia-hua-xian">eat</span> apples every day</li>
                <li>7. we <span class="xia-hua-xian">like</span> the teacher</li>
                <li>8. I <span class="xia-hua-xian">believe</span> him</li>
                <li>9. I <span class="xia-hua-xian">believe</span> you</li>
                <li>10. I <span class="xia-hua-xian">believe</span> them</li>
                <li>11. I <span class="xia-hua-xian">believe</span> my wife</li>
            </ul>
            <h3 class="biao-ti-1">4、名词复数(主语)+及物动词原型(谓语)+宾语+状语</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型里面的主语最好表示人的名词的复数，表示人的名词直接从名词表里面找。虽然名词的复数可以直接作主
                语，但是一般情况，还是需要加限定词或定冠词。如果不加限定词或定冠词，就是泛指，有时候可能不符合逻辑。<br />
