            <ul>
                <li><span class="xia-hua-xian">the</span> Great Wall 长城</li>
                <li><span class="xia-hua-xian">the</span> Summer Palace 颐和园</li>
                <li><span class="xia-hua-xian">the</span> United States 美国</li>
            </ul>
            <p>
                "great"是一个形容词，"wall"是一个普通名词，"great wall"一起变成了一个专有名词"长
