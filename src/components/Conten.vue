            <h1 class="biao-ti-1">代词的定义</h1>
            <p>
                <span class="ding-yi">代词是为了避免重复而用来代替名词的词语。</span><br />
                英语代词可分为以下九类：<br />
                <span class="xia-hua-xian">人称</span>代词、<span class="xia-hua-xian">物主</span>代词、<span
                    class="xia-hua-xian">反身</span>代词、<span class="xia-hua-xian">相互</span>代词、<span
                    class="xia-hua-xian">指示</span>代词、<span class="xia-hua-xian">不定</span>代词、<span
                    class="xia-hua-xian">疑问</span>代词、<span class="xia-hua-xian">连接</span>代词和<span
                    class="xia-hua-xian">关系</span>代词。<br />
                大多数代词具有名词和形容词的作用。代词的使用频率很高，而且种类多。有些代词用法比较
                复杂，为了让大家更好入门，所以这节课先讲三个代词，即：人称代词、物主代词和指示代词。
            </p>
            <h1 class="biao-ti">人称代词</h1>
            <p>
                <span class="ding-yi大多数人称代词就是代替表示人的名词的词语"></span><br />
                就是中文的：我、 你、 他、 她、它、 我们、 你们、 他们、 她们、 它们(英语 they 和 them 就包含了中文的：他们 她们 它们，注意：其实 it 和 they,them 可以代替物)<br />
                不过英语的人称代词有两种格式，而中文只有一种。这两种格式就是主格和宾格。
