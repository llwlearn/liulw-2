            <h1 class="biao-ti">主语+系动词+表语</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"<span
                    class="ding-yi">主语+be+表语</span>"句型在一般现在时态里面的构成方式很简单，就是把里面的"be"变成"is /are /am"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当主语是"I"的时候就用"am"，"am"很特殊，只有主语是"I"的时候才能用；当主语是单数的时候用"is"；<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当主语是复数的时候用"are"。另外，"you"也是用"are"，虽然"you"也有单数"你"的意思。主语和表语都不需要变化。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(当然，如果是<span class="xia-hua-xian">名词作主语和表语</span>，要<span
                    class="xia-hua-xian">注意可数名词的问题</span>。)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为了更方便学习造句，可以把"主语+be+表语"这个句型分成四个更具体详细的小句型：
            </p>
            <ul>
                <li>1. 人称代词主格(主语)+is/ are/ am +形容词(表语)</li>
                <li>2. 名词(主语)+ is/ are +形容词(表语)</li>
                <li>3. 人称代词主格(主语)+is/ are/ am+名词(表语)</li>
                <li>4. 名词(主语)+is/ are +名词(表语)</li>
            </ul>
            <h3 class="biao-ti-1">(一)人称代词主格(主语)+is/ are/ am +形容词(表语)</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型非常简单，在这个句型里面，我们用<span
                    class="xia-hua-xian">人称代词主格</span>充当主语，用<span class="xia-hua-xian">形容词</span>充当表语，然后，只要注意 is/ are/
                am 的用法就可以了。每个<span class="xia-hua-xian">人称代词主格</span>后面跟的 be 都是非常固定的，只要记住下面的搭配就可以了：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I am &nbsp;&nbsp;/You are &nbsp;&nbsp;/She is &nbsp;&nbsp;/He
                is &nbsp;&nbsp;/It is &nbsp;&nbsp;/We are &nbsp;&nbsp;/They are<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;掌握了以上的搭配，就剩下形容词作表语的问题了。只要从形容词表格里面随便拿一个形容词放在以上任何一个搭配后
                面，就可以构成英语最简单的句子了。当然，要注意逻辑问题。以下用四个最简单的形容词作表语，进行造句示范：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.Happy 快乐的 &nbsp;&nbsp;2.rich 有钱的 &nbsp;&nbsp;3.Busy 忙的
                &nbsp;&nbsp;4. famous 出名的
            </p>
            <ul>
                <li>1. I am happy. 我快乐</li>
                <li>2. you are happy.你快乐</li>
                <li>3. she is rich.她有钱</li>
                <li>4. he is busy.他忙</li>
                <li>5. it is famous.它是著名的(它=可能是某产品)</li>
                <li>6. we are famous. 我们出名</li>
                <li>7. they are busy.他们忙</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(is/are/am 中文的意思都是"是"，不过有时候在中文里面是不需要翻译出来，这是中文的原因，不代表 is/ are
                /am 没
                有意思。"I am happy"如果翻译成，"我是快乐的"，也没有什么错，中文很少这么说话。)
            </p>
            <h3 class="biao-ti-1">(二)名词(主语)+ is/ are +形容词(表语)</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型也比较简单，在这个句型里面，我们用<span
                    class="xia-hua-xian">名词</span>充当主语，继续用形容词充当表语。不过，因为只有"I" 才能
                用"am"，所以<span class="xia-hua-xian">名词</span>作主语只有系动词"is"和"are"。不同的名词，使用 is 和 are
                的情况也不同。虽然可以说名词单数用"is"，<br />
                名词复数用"are"，但是具体的情况还是比较麻烦的，所以下面用具体的情况说明"is"和"are"的使用情况：
            </p>
            <p>
                第一种情况：专有名词里面的<span class="xia-hua-xian">人名和地名</span>作主语的时候，<span class="xia-hua-xian">一个人名</span>或<span
                    class="xia-hua-xian">一个地名</span>作主语用"is"。
                比如：
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">Nick</span> is busy.尼克忙。</li>
                <li>2. <span class="xia-hua-xian">Nick</span> is famous.尼克出名。</li>
                <li>3. <span class="xia-hua-xian">Guangxi</span> is famous.广西出名。</li>
                <li>4. <span class="xia-hua-xian">Beijing</span> is big. 北京大。</li>
                <li style="list-style: none;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果是并列的人名或地名作主语，则用"are"：</li>
                <li>1. <span class="xia-hua-xian">Nick and Jack</span> are busy.尼克和杰克忙。</li>
                <li>2. <span class="xia-hua-xian">Guangzhou and Beijing</span> are big.广州和北京大。
                </li>
            </ul>
            <p>
                第二种情况：可数名词单数作主语，用"is"，但是要<span class="xia-hua-xian">注意可数名词单数</span>不能单独使用，要考
                虑加限定词或冠词。
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">The</span> student is happy. 这个学生开心。</li>
                <li>2. <span class="xia-hua-xian">This</span> student is happy. 这个学生开心。</li>
                <li>3. <span class="xia-hua-xian">My</span> student is rich. 我的学生有钱。</li>
                <li>4. <span class="xia-hua-xian">Nick’s</span> student is busy. 尼克的学生忙。</li>
                <li>5. <span class="xia-hua-xian">The banana</span> is cheap/delicious.这香蕉便宜/好吃。</li>
            </ul>
            <p>
                第三种情况：可数名词复数作主语，用"are"，可数名词复数直接作主语，强调泛指，如果是
                特指，依然需要考虑加定冠词或限定词。
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">Teachers</span> are busy.老师忙。(泛指大部分老师)</li>
                <li>2. <span class="xia-hua-xian">The teachers</span> are busy.这些老师忙。(特指这些老师)</li>
                <li>3. <span class="xia-hua-xian">The students</span> are busy.这些学生忙。</li>
                <li>4. <span class="xia-hua-xian">My students</span> are busy.我的学生忙。</li>
            </ul>
            <p>
                第四种情况：不可数名词作主语，属于单数概念，用"is"，不可数名词直接作主语，也是强
                调泛指，如果是特指，依然需要考虑加定冠词或限定词。
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">Water</span> is important.水是重要的。(泛指大部分的水)</li>
                <li>2. <span class="xia-hua-xian">Oil</span> is expensive.油贵。(泛指大部分的油)</li>
                <li>3. <span class="xia-hua-xian">The water</span> is important.这水是重要的。(特指这部分水)</li>
            </ul>
            <h3 class="biao-ti-1">(三)人称代词主格(主语)is/ are/ am+名词(表语)</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型的<span
                    class="xia-hua-xian">主语和系动词部分</span>跟第一种小句型一样，唯一不同的是表语部分。这里，表语使用名词充当。名词作表语
                比形容词作表语麻烦，因为使用名词的时候，不仅仅要考虑可数名词单复数规则，还得注意逻辑问题。之前的课程有提到一
                个问题，抽象名词不能乱作表语。所以，作为初学者，建议先使用表示人的名词作表语。(当然其他名词也可以作表语，只
                是相对用得少，并且容易出现逻辑。)
            </p>
            <p>
                表示人的名词有两种：
            </p>
            <p>
                第一种：人名。所以任何一个人名都可以作表语，并且不需要任何变化。直接把一个人名放
                在表语的位置，就可以构成一个句子了：
            </p>
            <ul>
                <li>1. I am <span class="xia-hua-xian">Nick.</span></li>
                <li>2. She is <span class="xia-hua-xian">Jenny.</span></li>
                <li>3. He is <span class="xia-hua-xian">Tony.</span></li>
            </ul>
            <p>
                第二种：名词表里面表示职业身份的名词。但是这种名词多数是可数名词，要注意加限定词，
                冠词或变复数问题。
            </p>
            <ul>
                <li>1. I am <span class="xia-hua-xian">a doctor</span></li>
                <li>2. You are <span class="xia-hua-xian">a student</span></li>
                <li>3. you are <span class="xia-hua-xian">students</span></li>
                <li>4. She is <span class="xia-hua-xian">a nurse.</span></li>
                <li>5. She is <span class="xia-hua-xian">my student</span></li>
                <li>6. He is <span class="xia-hua-xian">Nick’s student</span></li>
                <li>7. It is <span class="xia-hua-xian">my room</span> (it 可以指代事物，所以表语也可以表示事物)</li>
                <li>8. It is <span class="xia-hua-xian">my dog</span></li>
                <li>9. They are <span class="xia-hua-xian">teachers</span></li>
                <li>10. They are <span class="xia-hua-xian">my teachers</span></li>
                <li>11. We are <span class="xia-hua-xian">students/guides/actors</span></li>
            </ul>
            <h3 class="biao-ti-1">(四)名词 is/are +名词</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个小句型，比较麻烦一点，因为，<span class="xia-hua-xian">名词</span>同时作<span
                    class="xia-hua-xian">主语</span>和<span class="xia-hua-xian">表语</span>，如果出现可数名词，都要注意可数名词的规则，更重要的
                是，容易出现逻辑问题。多数情况下，<span class="xia-hua-xian">主语是表示人的名词，</span><span
                    class="xia-hua-xian">表语也是表示人的名词</span>。当然其他的名词也可以，只是表示人
                的名词，更容易符合逻辑。
            </p>
            <p>
                第一种情况：专有名词里面的人名和地名作主语的时候，<span class="xia-hua-xian">一个人名</span>或<span
                    class="xia-hua-xian">一个地名</span>作主语用"is"。
                比如：
            </p>
            <ul>
                <li>Nick <span class="xia-hua-xian">is</span> a teacher</li>
                <li>如果是并列的人名或地名作主语，则用"are"：</li>
                <li>Nick and Jack <span class="xia-hua-xian">are</span> teachers</li>
            </ul>
            <p>
                第二种情况：可数名词单数作主语，用"is"。但是要注意可数名词单数不能单独使用，要考
                虑加限定词或冠词。
            </p>
            <ul>
                <li>My father <span class="xia-hua-xian">is</span> a farmer</li>
                <li>His sister <span class="xia-hua-xian">is</span> a nurse</li>
                <li>My teacher <span class="xia-hua-xian">is</span> Nick</li>
                <li>The doctor <span class="xia-hua-xian">is</span> my brother</li>
            </ul>
            <p>
                第三种情况：可数名词复数作主语，用"are"。可数名词复数直接作主语，强调泛指。如果是
                特指，依然需要考虑加冠词或限定词。
            </p>
            <ul>
                <li>The Teachers <span class="xia-hua-xian">are</span> my friends</li>
                <li>My students <span class="xia-hua-xian">are</span> bosses</li>
            </ul>
            <p>
                第四种情况，不可数名词作主语，用"is"。不可数名词可以直接作主语，强调泛指。如果是
                特指，依然需要考虑加定冠词或限定词。
            </p>
            <ul>
                <li>Rice <span class="xia-hua-xian">is</span> important food (泛指)</li>
                <li>The rice <span class="xia-hua-xian">is</span> my breakfast(特指)</li>
            </ul>
            <h3 class="biao-ti-1">(五)"主语+be+表语"句型在一般现在时态里面变否定句的规则.</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个规则非常简单，只要在肯定句 be (is /are /am)后面加一个否定词 "not"就可以了。"is
                not"和"are not"可以缩写
                为"isn’t"和"aren’t"，请看下面的例子：
            </p>
            <ul>
                <li>肯定句：I <span class="xia-hua-xian">am</span> a teacher. 我是老师</li>
                <li>否定句：I <span class="xia-hua-xian">am not</span> a teacher. 我不是老师</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：She <span class="xia-hua-xian">is</span> pretty . 她漂亮</li>
                <li>否定句：She <span class="xia-hua-xian">is not</span> /isn’t pretty. 她不漂亮 </li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：You <span class="xia-hua-xian">are</span> great. 你伟大 </li>
                <li>否定句：You <span class="xia-hua-xian">are not</span> /aren’t great. 你不伟大</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：They <span class="xia-hua-xian">are</span> farmers.他们是农民</li>
                <li>否定句：They <span class="xia-hua-xian">are not</span> farmers 他们不是农民</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：We <span class="xia-hua-xian">are</span> students. 我们是学生</li>
                <li>否定句：We <span class="xia-hua-xian">are not</span> students 我们不是学生</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：It <span class="xia-hua-xian">is</span> a book. 这是一本书</li>
                <li>否定句：It <span class="xia-hua-xian">is not</span> a book. 这不是一本书</li>
            </ul>
            <h3 class="biao-ti-1">(六)"主语+be+表语"句型在一般现在时态里面变一般疑问句的规则。</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个规则也很简单，只要把肯定句里面的 be(is/ are /am)移到句首，加问号就可以了。不过，有时候，要根据实际情
                况，把主语的人称改变一下，因为人称改变了，be(is /are /am)也要根据人称的变化而发生变化，请看下面例句：
            </p>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span
                    class="xia-hua-xian">为了更清晰地展现肯定句变一般疑问句的规则，下面把"一般疑问句"简写为"疑问句"但是请注意，这里的"疑问句"真
                    正的名称是"一般疑问句"。</span>
            </p>
            <ul>
                <li>肯定句：I <span class="xia-hua-xian">am</span> a teacher 我是老师。</li>
                <li>疑问句：<span class="xia-hua-xian">Are</span> you a teacher?你是老师吗？</li>
                <li style="list-style: none">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意，一般不说，Am I a
                    teacher？不会自己问自己的。因为主语人称发生了变化，主语
                    换成了 you,系动词跟着变成 are</li>
                <li>肯定回答：yes,I am 是的，我是</li>
                <li>否定回答：no.I am not 不，我不是</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：She <span class="xia-hua-xian">is</span> pretty . 她漂亮 </li>
                <li>疑问句：<span class="xia-hua-xian">Is</span> she pretty? 她漂亮吗？</li>
                <li>肯定回答：yes ,she is 是的，(她漂亮)</li>
                <li>否定回答：no, she is not(isn’t)不，(她不漂亮)括号的文字，中文里面不需要翻译的。</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：You <span class="xia-hua-xian">are</span> great. 你伟大 </li>
                <li>疑问句：<span class="xia-hua-xian">Are</span> you great? 你伟大吗？</li>
                <li>肯定回答：yes ,I’m 是的，我伟大 </li>
                <li>否定回答：no,I am not 不，我不伟大</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：They <span class="xia-hua-xian">are</span> farmers. 他们是农民</li>
                <li>疑问句：<span class="xia-hua-xian">Are</span> they farmers？他们是农民吗？</li>
                <li>肯定回答：Yes，they are .是的，他们是</li>
                <li>否定回答：no ,they are not 不，他们不是</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：We <span class="xia-hua-xian">are</span> students. 我们是学生 </li>
                <li>疑问句：<span class="xia-hua-xian">Are</span> you students？你们是学生吗？</li>
                <li>肯定回答：Yes，we are 是的，我们是</li>
                <li>否定回答：no，we are not(aren’t)不，我们不是</li>
                <li style="list-style: none"><br /></li>
                <li>肯定句：It <span class="xia-hua-xian">is</span> a book. 这是一本书 </li>
                <li>疑问句：<span class="xia-hua-xian">Is</span> it a book？ 这是一本书吗？ </li>
                <li>肯定回答：Yes,It is,是的</li>
                <li>否定回答：no ,it isn’t 不，不是</li>
            </ul>
