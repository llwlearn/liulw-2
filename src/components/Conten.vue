            <h1 class="biao-ti">物主代词</h1>
            <p>
                <span class="ding-yi">表示所有关系的代词叫物主代词。</span><br />
                物主代词包括<span class="xia-hua-xian">形容词性物主代词</span>和<span class="xia-hua-xian">名词性物主代词</span>。
            </p>
            <h2 class="biao-ti-1">1、形容词性物主代词</h2>
            <table>
                <tr>
                    <td>my</td>
                    <td>your</td>
                    <td>her</td>
                    <td>his</td>
                    <td>its</td>
