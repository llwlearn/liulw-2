            <h1 class="biao-ti">形容词的用法</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词的用法很容易掌握，因为用法不多，一般只可以作<span
                    class="xia-hua-xian">定语</span>、<span class="xia-hua-xian">表语</span>和<span
                    class="xia-hua-xian">宾补</span>。
            </p>
            <h2 class="biao-ti-1">1、做表语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大部分的形容词都可以作表语。形容词作表语可以直接构成一个句型：<span class="xia-hua-xian">主语
                    +be(系动
                    词)+形容词(表语)</span>
            </p>
            <ul>
                <li>1. I am <span class="xia-hua-xian">happy/busy</span>. 我快乐/我忙。(形容词 happy/busy 作表语)</li>
                <li>2. You are <span class="xia-hua-xian">rich</span>.你是有钱的。(形容词 rich 作表语)</li>
                <li>3. She is <span class="xia-hua-xian">busy</span>.她忙。(形容词 busy 作表语)</li>
                <li>4. He is <span class="xia-hua-xian">happy</span>.他快乐。(形容词 happy 作表语)</li>
                <li>5. They are <span class="xia-hua-xian">rich</span>.他们/她们是有钱的。(形容词 rich 作表语)</li>
                <li>6. We are <span class="xia-hua-xian">happy</span>.我们快乐。(形容词 happy 作表语)</li>
                <li>7. It is <span class="xia-hua-xian">cheap/big/small</span>.它是便宜的/大的/小的。(形容词 cheap/big/small 作表语)
                </li>
                <li>8. The house is <span class="xia-hua-xian">big</span>.这个房子是大的。(形容词 big 作表语)</li>
                <li>9. My house is <span class="xia-hua-xian">small</span>.我的房子是小的。(形容词 small 作表语)</li>
                <li>10. The film is very <span class="xia-hua-xian">boring</span>.这部电影很无聊。(形容词 boring 作表语)</li>
                <li>11. A good student must be <span class="xia-hua-xian">diligent</span>.一个好学生一定是勤奋的。(形容词 diligent 作表语)
                </li>
                <li>12. She is always <span class="xia-hua-xian">happy</span>.她总是很开心。(形容词 happy 作表语)</li>
                <li>13. They are very <span class="xia-hua-xian">hard-working</span>.他们很努力。(形容词 hard-working 作表语)</li>
            </ul>
            <h2 class="biao-ti-1">2、作前置定语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大部分的形容词都可以作前置定语，修饰普通名词，放在普通名词的前面。比如：
            </p>
            <ul>
                <li>The <span class="xia-hua-xian">big</span> House 这个大房子。(形容词 big 作前置定语修饰名词 house)</li>
                <li>My <span class="xia-hua-xian">small</span> house 我的小房子。(形容词 small 作前置定语修饰名词 house)</li>
                <li>A <span class="xia-hua-xian">cheap</span> computer 一台便宜的电脑。(形容词 cheap 作前置定语修饰名词 computer)</li>
                <li>A <span class="xia-hua-xian">happy</span> girl 一个快乐的女孩。(形容词 happy 作前置定语修饰名词 girl)</li>
                <li>A <span class="xia-hua-xian">rich</span> man 一个有钱的男人。(形容词 rich 作前置定语修饰名词 man)</li>
                <li><span class="xia-hua-xian">busy</span> people 忙碌的人们。(形容词 busy 作前置定语修饰名词 people)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意，形容词作前置定语，只跟名词有关系，修饰一个普通名词。所以<span
                    class="xia-hua-xian">形容词直接作定语只能构成一个短语</span>。如果要用
                形容词作定语造句，必须考虑普通名词的问题。也就是说，先拿一个普通名词造句，然后在这个普通名词前面增加一个形容
                词或多个形容词作前置定语。
            </p>
            <ul>
                <li>1. I like the <span class="xia-hua-xian">small</span> house. 我喜欢这个小房子。</li>
                <li>2. I bought a <span class="xia-hua-xian">cheap</span> computer. 我买了一台便宜的电脑。</li>
                <li>3. The <span class="xia-hua-xian">cheap</span> computer is mine.这台便宜的电脑是我的。</li>
                <li>4. Those <span class="xia-hua-xian">young</span> people like sports very much.那些年轻人非常喜欢运动。</li>
                <li>5. My mother bought an <span class="xia-hua-xian">interesting</span> book for me
                    yesterday.我妈妈昨天买了一本有趣的书给我。</li>
                <li>6. I have a <span class="xia-hua-xian">big</span> house.我有一个大房子。</li>
                <li>7. She is a <span class="xia-hua-xian">clever</span> girl.她是一个聪明的女孩子。</li>
            </ul>
            <h2 class="biao-ti-1">3、作宾语补足语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词可以作宾补，但是<span
                    class="xia-hua-xian">只有部分特定的及物动词后面才可以跟形容词作宾补</span>。比如：Make(使，让), keep(保持),
                prove (证明是)等等。请看例句：
            </p>
            <ul>
                <li>1)I make you <span class="xia-hua-xian">busy</span>.我让你忙。</li>
                <li>2)I make you <span class="xia-hua-xian">rich</span>.我让你有钱。</li>
                <li>3)We keep the room <span class="xia-hua-xian">clean</span>.我们保持房间干净。</li>
                <li>4)We keep the classroom <span class="xia-hua-xian">quiet</span>.我们保持教室安静。</li>
                <li>5)We must keep the classroom <span class="xia-hua-xian">clean</span>.我们必须保持教室清洁。</li>
                <li>6)All the facts proved him <span class="xia-hua-xian">right</span>. 所有的事实都证明了他是对的。</li>
                <li>7)She made me <span class="xia-hua-xian">angry</span> last night. 她昨天晚上让我生气了。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般来说，形容词作定语和表语的情况是最多的，作宾补的情况相对少些。
            </p>
        </div>
