            <h1 class="biao-ti">形容词的位置</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当出现<span
                    class="xia-hua-xian">几个形容词同时修饰名词作前置定语的时候</span>，要考虑这几个形容词的位置。这些形容词的排序跟中文不一样的。<br />
                下面就是位置的排放顺序：<br />
                1. <span class="xia-hua-xian">冠词</span>(a,an,the 物主代词 my,数词 three,指示代词等)+2. <span
                    class="xia-hua-xian">描绘性的形容词</span> beautiful+3. 表示<span
                    class="xia-hua-xian">形状</span>(大小,长短,高矮)的<span class="xia-hua-xian">形容词</span><br />
                +4. 表示<span class="xia-hua-xian">年龄</span>或者<span class="xia-hua-xian">新旧</span>的<span
                    class="xia-hua-xian">形容词</span>+5. 表示<span class="xia-hua-xian">颜色</span>+6. 表示<span
                    class="xia-hua-xian">出处,来源</span>,+7. 表示<span class="xia-hua-xian">材料,物质的形容词</span>+8. 表示<span
                    class="xia-hua-xian">用途</span>
            </p>
            <ul>
                <li>1. a <span class="xia-hua-xian">beautiful</span>,<span class="xia-hua-xian">tall</span>,<span
                        class="xia-hua-xian">young</span> girl 一个年轻漂亮，身材高挑的女孩子</li>
                <li>2. my <span class="xia-hua-xian">nice</span> <span class="xia-hua-xian">small</span> <span
                        class="xia-hua-xian">brown</span> <span class="xia-hua-xian">leather</span> bag 我漂亮的棕色小皮包
                </li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;虽然冠词(a,an,the 物主代词 my,数词 three,指示代词) 不属于形容词，但是<span
                    class="xia-hua-xian">永远要放在作定语的形容词前面</span>。如：
            </p>
            <ul>
                <li>1. She is my <span class="xia-hua-xian">kind</span> teacher.她是我善良的老师。</li>
                <li>2. She is a <span class="xia-hua-xian">young</span> teacher.她是一个年轻的老师</li>
                <li>3. I have three <span class="xia-hua-xian">old</span> books.我有三本旧书。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般情况下，同时修饰一个名词的形容词的数量不会超过 4 个，多了就不好了。基本上都是：<br />
                1. <span class="xia-hua-xian">冠词</span>(a,an,the 物主代词 my,数词 three,)+2. <span
                    class="xia-hua-xian">描绘性的形容词</span> beautiful+3. 表示<span
                    class="xia-hua-xian">形状</span>(大小,长短,高矮)的<span class="xia-hua-xian">形容词</span><br />
                +4. 表示<span class="xia-hua-xian">年龄</span>或者<span class="xia-hua-xian">新旧</span>的<span
                    class="xia-hua-xian">形容词</span>+5. 表示<span class="xia-hua-xian">颜色</span>。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而且，一般情况下，没有第 2 种形容词，第 3 种就放在第 1 的后面，没有表示第 3 种形容词，第 4 种就排在第 2
                种的
                后面，依次类推。(I have an <span class="xia-hua-xian">old red</span> book.不能说，I have a <span
                    class="xia-hua-xian">red old</span> book. 按照之前的规则，"old"是表示年龄的形容词
                应该排在表示颜色的形容词 "red"前面 。)
            </p>
