            <h1 class="biao-ti">什么是时态</h1>
            <p>
                英语造每一句话，都要考虑时态问题。<br />
                时态是英语语法的一大特色，也是和中文语法的最大区别之一。<br />
                时态的定义：<span class="ding-yi">作谓语的动词用来表示动作发生或者状态存在时间的各种形式称为时态。</span><br />
                比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我昨天买了一本书 &nbsp;&nbsp;I <span
                    class="xia-hua-xian">bought</span> a book yesterday<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我明天要买一本书 &nbsp;&nbsp;I <span class="xia-hua-xian">will</span>
                <span class="xia-hua-xian">buy</span> a book tomorrow<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我每天买一本书 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span
                    class="xia-hua-xian">buy</span> a book every day<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;通过对比，我们不难发现，中文和英文的时间状语都要发生变化，但是中文的动词没有发生变化，都是"买"，而英文的
                谓语动词发生了变化："bought" ,"will buy","buy"，另外，中文的时间状语放在中间，而英文的时间状语放在句尾(也可以
                放在句首，但是不常用，主要出现在书面语里面)。
            </p>
            <hr />
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;现在我们就明白了，时态其实就是<span
                    class="xia-hua-xian">一个谓语动词随着时间的变化而发生的不同形态变化</span>，所以简称“<span
                    class="xia-hua-xian">时态</span>”。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;谓语动词在各种不同的时间里发生的变化称为不同的时态。<br />
                比如,发生在过去的变化叫做过去时态；在将来的时间发生的变化，叫做将来时态。<br />
                英语有 16 种时态，常用的有 10 种：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般现在时态，一般过去时态，一般将来时态，现在进行时态，现在完成时态，<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;过去完成时态，过去将来时态，过去进行时态，现在完成进行时态，将来进行时态。<br />
                其他的六种比较少用，并且主要出现在书面语里。本教程先讲五种最简单常用的时态：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="xia-hua-xian">一般现在时态</span>，<span
                    class="xia-hua-xian">一般过去时态</span>，<span class="xia-hua-xian">一般将来时态</span>，<span
                    class="xia-hua-xian">现在进行时态</span>，<span class="xia-hua-xian">现在完成时态</span>。<br />
                另外五种在下一个教程里面讲述。
            </p>
            <hr />
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;时态是英语语法的一个重点内容。因为中文没有时态变化，所以对于初学者来说可能有点难度。其实时态的构成方式并
                不难，绝大部分的人可以掌握。难的地方在于，很多人不知道如何运用恰当的时态去表达正确的意思。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如何使用正确的时态是我们学习时态的主要目的，而不仅仅是掌握构成方式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在使用英语造句的时候，<span
                    class="xia-hua-xian">每一句话都要考虑时态的问题</span>，因为每句话所表达的动作或状
                态在不同的时间里面，谓语动词的变化是不一样的。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前面学习句子成分的时候提到，我们说的每一句话都可以分为动作的和状态的。也就是两个基本句型：<br />
                <span class="xia-hua-xian">动作的句型</span>(主语+谓语+宾语)，<span
                    class="xia-hua-xian">状态的句型</span>(主语+be/系+表语)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这两种句型的时态构成方式是不一样的。所以，以后讲每个时态的时候，都要分别讲解这两个句型的构成方式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下面我们来学习第一个时态：一般现在时态
            </p>
