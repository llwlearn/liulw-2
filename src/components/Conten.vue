            <h1 class="biao-ti">名词的分类</h1>
            <p>
                <span class="ding-yi">普通名词和专有名词。</span><br />
                普通名词又分为<span class="xia-hua-xian">可数名词</span>和<span class="xia-hua-xian">不可数名词</span>。<br />
                它们的用法和变化区别很大。<br />
                如果不注意分清可数名词和不可数名词，很容易用错。
            </p>
            <hr />

            <p>
                <span class="xia-hua-xian">可数名词</span>就是那些肉眼能分清数量的名词，比如：书，房子，手机，电脑等等。<br />
                <span class="xia-hua-xian">不可数名词</span>刚好相反，就是肉眼分不清数量的名词，比如大部分的液体：水、牛奶、酒、茶、头发、砂、肉、大米等等。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有时候，可数不可数，跟中文不一样。中文里面不可数的名词，在英文是可数的。比如："天空"，在中文里面是不可
                数，英文"sky"是可数的，复数形式是"skies"。"记忆"在中文是不可数的，英文"memory"是可数的，复数形式是：<br />
                "memories"。同样是液体，"water"是不可数的，"眼泪 tear"却是可数的，复数形式是"tears"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我们造句的时候，要注意这些不同的情况，但也<span
                    class="xia-hua-xian">不要死记规则</span>。因为这样只能把英语变得更复杂，增加我们学习的压
                力。买一本英文字典，学到新的名词，查一下字典。字典里面会说清楚名词是可数还是不可数的，最好是牛津英汉双语词
                典。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;每个可数名词都有两种格式：一个是<span
                    class="xia-hua-xian">单数的格式</span>，另一个是<span class="xia-hua-xian">复数的格式</span>。复数的格式都是由单数变化而成的。有规则的
                变化和无规则的变化。大部分名词单数变复数都是有规则的变化。

            </p>
        </div>
