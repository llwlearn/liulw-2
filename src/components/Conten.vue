            <h1 class="biao-ti">主语(单三)+谓语+宾语</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二种构成方式：当主语是第三人称单数的时候，谓语动词要变成第三人称单数的格式。可能很多人都知道什么是第三
                人称单数了，这里还是要重复讲一下，因为有很多人并没有真正弄明白第三人称单数。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span
                    class="xia-hua-xian">所谓第三人称单数，就是：不是"我"，不是"你"，而是第三者(人或者物)，并且一定是一个单数。</span><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;很多人没有弄清楚，第三人称单数，主要因为受名词前面的定语影响，比如："your father"，很多人把"your
                father"
                理解成第二人称，因为有一个"your"，但是这里的"your"只是一个定语，"你的爸爸"不是"你"，也不是"我"，是第三个人，而且是一个人，
                是单数概念。所以"your father"是第三人称单数，并非第二人称，定语"your"不能决定人称。
            </p>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大概什么词语作主语属于第三人称单数：
            </p>
            <ul>
                <li>1. 人称代词主格：she,he ,it</li>
                <li>2. 所有的名词的单数：train,bus,Father,Mother, doctor</li>
                <li>3. 一个人名/一个地名，都是第三人称单数:Nick,Jack, Guangxi.</li>
                <li>4. 不可数名词也是第三人称单数:water,sugar,wine</li>
                <li>5. 指示代词 this,that 也是第三人称单数</li>
                <li>6. 动词不定式和动名词短语</li>
                <li>7. 主语从句</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为了更方便学习造句，可以把"主语(第三人称单数)+谓语 +宾语"这个句型分成四个更具体详细的小句型：
            </p>
            <h3 class="biao-ti-1">1、人称代词主格(小三)+不及物动词小三格式(谓语)+状语</h3>
