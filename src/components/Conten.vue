            <h1 class="biao-ti">定冠词"the"的用法</h1>
            <h3 class="biao-ti-1">1.在普通单数名词前指一类人或者事物，不必翻译，跟不定冠词的第二种用法一样，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">The</span> ear is an organ for listening. 耳朵是听的器官</li>
                <li><span class="xia-hua-xian">The</span> fox is a cunning animal. 狐狸是狡猾的动物</li>
                <li><span class="xia-hua-xian">The</span> dog is a cute animal. 狗是可爱的动物。</li>
                <li style="list-style: none">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，表达一类人或者事物，还可以名词的复数表达，如：</li>
                <li><span class="xia-hua-xian">Foxes</span> are cunning animals. 狐狸是狡猾的动物</li>
                <li><span class="xia-hua-xian">Dogs</span> are cute animals. 狗是可爱的动物</li>
            </ul>
            <h3 class="biao-ti-1">2.在重新提及的人或事物前，含有"这，这些"的意思，可以修饰可数名词单数，复数，也可以修饰不可数名词，如：</h3>
            <ul>
                <li>I bought a book yesterday. <span class="xia-hua-xian">The</span> book is
                    interesting.我昨天买了一本书，这本书很有趣。</li>
                <li>I know a friend. <span class="xia-hua-xian">The</span> friend is a doctor 我认识一个朋友 ，这个朋友是一个医生。</li>
                <li>She bought three pencils,<span class="xia-hua-xian">the</span> pencils are for her daughter
                    她买了三支铅笔，这些铅笔是给她女儿的。</li>
                <li>I drank tea just now, <span class="xia-hua-xian">the</span> tea is very good.我刚才喝茶了，这茶很不错。</li>
            </ul>
            <h3 class="biao-ti-1">3.在谈话双方都知道的人或物前面，同样可以修饰可数名词单数，复数，也可以修饰不可数名词。如：</h3>
            <ul>
                <li>pass me <span class="xia-hua-xian">the</span> book ,please！请把这本书递给我。</li>
                <li>pass me <span class="xia-hua-xian">the</span> books ,please！请把这些书递给我。</li>
                <li style="list-style: none">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这里谈话双方都知道"the book"和"the
                    books"是指示哪些书。</li>
                <li>Pass <span class="xia-hua-xian">the</span> salt to me，please!请把这盐递给我。</li>
            </ul>
            <h3 class="biao-ti-1">4.在有限定性后置定语修饰的名词前，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">the</span> people <span class="xia-hua-xian">in the park</span> 在公园里面的人
                </li>
                <li><span class="xia-hua-xian">the</span> girl <span class="xia-hua-xian">in red</span> 穿红色衣服的女孩</li>
                <li><span class="xia-hua-xian">the</span> price <span class="xia-hua-xian">of the house</span> 房子的价格
                </li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上面的名词都是带介词短语作<span
                    class="xia-hua-xian">限定性后置定语</span>，所以都要加定冠词"the"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="xia-hua-xian">The</span> girl <span
                    class="xia-hua-xian">dancing with my brother</span> is his sister 正在和我哥哥跳舞的女孩是他的妹妹。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这是动名词短语作限定性后置定语修饰名词"girl"，所以"girl"前面要加"the"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意：以上的第 2，3，4 种定冠词"the"的用法是最重要也是最常用的。其实，这三种用法，可以总结为一种用法，也
                就是：凡是特指的名词，都加定冠词"the"，不管是可数名词单数，复数，还是不可数名词。
            </p>
            <h3 class="biao-ti-1">5.在序数词前面，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">the</span> first 第一</li>
                <li><span class="xia-hua-xian">the</span> second 第二</li>
                <li><span class="xia-hua-xian">the</span> third 第三</li>
            </ul>
            <h3 class="biao-ti-1">6.在表示世界独一无二的事物的名词前，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">the</span> earth 地球</li>
                <li><span class="xia-hua-xian">the</span> sun 太阳</li>
                <li><span class="xia-hua-xian">the</span> world 世界</li>
                <li><span class="xia-hua-xian">the</span> moon 月亮</li>
            </ul>
            <h3 class="biao-ti-1">7.在表示时间或其他情况的习惯用语前，如：</h3>
            <ul>
                <li>in <span class="xia-hua-xian">the</span> end 最后</li>
                <li>in <span class="xia-hua-xian">the</span> morning /afternoon/evening 在早上/下午/晚上</li>
                <li><span class="xia-hua-xian">the</span> same 同样</li>
            </ul>
            <h3 class="biao-ti-1">8.在形容词和副词的最高级前，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">the</span> most important 最重要</li>
                <li><span class="xia-hua-xian">the</span> best 最好的</li>
                <li>His family is about a mile from <span class="xia-hua-xian">the</span> nearest river.他家离最近的河大概一英里远。
                </li>
            </ul>
            <h3 class="biao-ti-1">9.在某些形容词前,变成名词，如：</h3>
            <ul>
                <li><span class="xia-hua-xian">the</span> old 老人</li>
                <li><span class="xia-hua-xian">the</span> young 年轻人</li>
                <li><span class="xia-hua-xian">the</span> poor 穷人</li>
                <li><span class="xia-hua-xian">the</span> rich 富人</li>
                <li><span class="xia-hua-xian">the</span> weak 弱者</li>
                <li><span class="xia-hua-xian">the</span> wise 智者</li>
            </ul>
            <h3 class="biao-ti-1">10. 在姓氏复数前,表示一家人或者夫妇，如： </h3>
            <ul>
                <li><span class="xia-hua-xian">The</span> Smiths 史密斯一家人/史密斯夫妇</li>
                <li><span class="xia-hua-xian">The</span> Chens 陈家/陈家夫妇</li>
                <li><span class="xia-hua-xian">The</span> Greens 格林一家人/格林夫妇</li>
                <li><span class="xia-hua-xian">The</span> Whites 怀特一家人/怀特夫妇</li>
                <li>如果有孩子的，就指一家人，没有孩子的就指夫妇。</li>
            </ul>
