            <h1 class="biao-ti">宾语补足语</h1>
            <p>
                <span class="ding-yi">补充宾语的词语，就叫宾语补足语，简称宾补。</span><br />
                在英语里面，有些动词加上宾语之后，意思还是不完整，比如下面的动词：
            </p>
            <table>
                <tr>
                    <td>Make (使，让)</td>
                    <td>let (让) get (让)</td>
                    <td>have (让，使)</td>
                    <td>find (发现)</td>
                </tr>
                <tr>
                    <td>call (叫，称为)</td>
                    <td>see (看见)</td>
                    <td>keep (保持)</td>
                    <td>put (放)</td>
                </tr>
            </table>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我们用"make"这个词语来解释。"Make"是"使，让"的意思，如果我们说："I make
                you."(我让你，我使你)，意思不完<br />
                整。但是，如果加上一个词语"happy(快乐)"，这个句子意思就完整了："I make you happy,(我让你快乐)"。这个"happy"<br />
                就是我们说的<span class="xia-hua-xian">宾补</span>。<br />
                宾补只跟宾语有关系。<br />
                能作宾补的词语有：形容词、名词、数词、动词不定式、动名词、副词、分词、介词短语、句子。
            </p>
            <hr />

            <h2 class="biao-ti-1">1、形容词作宾补</h2>
            <ul>
                <li>1. I made her <span class="xia-hua-xian">happy</span>.我让她开心。</li>
                <li>2. You make me <span class="xia-hua-xian">mad</span>. 你让我发疯。</li>
                <li>3. She made him <span class="xia-hua-xian">upset</span>.她让他心烦</li>
                <li>4. He made the English class <span class="xia-hua-xian">funny</span>. 他让英语课变得有趣。</li>
                <li>5. I can’t make grammar classes <span class="xia-hua-xian">funny</span>. 我不能让语法课变得有趣。</li>
                <li>6. Eating apples can make you <span class="xia-hua-xian">beautiful/healthy</span>. 吃苹果可以让你变漂亮/健康。
                </li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">2、名词作宾补</h2>
            <ul>
                <li>1. I call her <span class="xia-hua-xian">Mary</span>. 我叫她玛丽</li>
                <li>2. You can call me <span class="xia-hua-xian">Nick</span>.你们可以叫我 Nick</li>
                <li>3. I will make her a happy <span class="xia-hua-xian">woman</span>.我将会让她成为一个幸福的女人。</li>
            </ul>
            <p>
                注意：名词作宾补的时候容易跟双宾混淆。<br />
                比如：I gave <span class="xia-hua-xian">her</span> a <span class="xia-hua-xian">book</span>.(双宾)<br />
                I call her Mary.(名词作宾补)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;双宾要求一个表示人，一个表示物体。而名词作宾补的时候，跟宾语是同位语关系，也就是说，宾语和宾补是同一个人<br />
                或同一个事物。"I call her <span
                    class="xia-hua-xian">Mary</span>."宾语"Her"和人名"Mary"其实是指同一个人，所以这个是宾补，不可能是双宾。<br />
                双宾必须是一个人，一个事物。
            </p>
            <hr />

            <h2 class="biao-ti-1">3、数词作宾补</h2>
            <p>
                I put my career/family <span class="xia-hua-xian">(the)</span> first.我把事业/家庭放在第一位。<br />
                (动词不定式，动名词，副词、分词、介词短语、句子作宾补的情况，暂时不举例说明，以后会细讲。)
