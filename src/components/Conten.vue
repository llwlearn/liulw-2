            <h1 class="biao-ti">情态动词</h1>
            <p>
                <span class="ding-yi">情态动词的作用就是跟动词原型(包括动词短语原型，系动词 be)构成复合谓语。</span><br />
                情态动词本身有意义，但是不完整。以下是常见的情态动词。
            </p>
            <table>
                <tr>
                    <td>Can〈能,会)</td>
                    <td>could (can的过去式)</td>
                    <td>may (可以)</td>
                    <td>might (may过去式)</td>
                </tr>
                <tr>
                    <td>should (应该)</td>
                    <td>must (必须)</td>
                    <td>have to (不得不)</td>
                    <td>dare (敢)</td>
                </tr>
                <tr>
                    <td>Needn't (不必)</td>
                    <td>had better (最好)</td>
                    <td>ought to (应该)</td>
                    <td>Be able to=can</td>
                </tr>
            </table>
            <p>
                情态动词的特点：
            </p>
            <ul>
                <li>1.情态动词必须跟动词原型构成复合谓语。</li>
                <li>2.情态动词是有意义的，虽然意思不完整。</li>
                <li>3.有专门的情态动词，不需要借用其他动词。</li>
                <li>4.情态动词一般不能跟助动词连用。</li>
            </ul>
            <hr />
            <p>
                情态动词使用情况：<br />
                当我们需要表达情态动词的意义，比如"必须""能""应该"等等的时候，就可以加上一个相应的情态动词，构成复合谓语。比如：
            </p>
            <ul>
                <li>1. I earn money every day.我每天赚钱。</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这句话没有情态动词，仅仅表达"每天赚钱"。但是，如果我们需要表达"必须每天赚钱"，强调"必须"这个意义。
                    我们就可以加一个情态动词 "must"，变成下面的句子：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">must</span> earn money every
                    day.我每天必须赚钱。<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这里的 "<span class="xia-hua-xian">must
                        earn</span>"就是"情态动词+动词原型"构成复合谓语。</li>
                <li>2. I help you 我帮助你。</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这句话没有情态动词，仅仅表达"我经常帮你"。如果要表达"我应该帮你"，强调"应该"这个意思，就可以加一个
                    情态动词"should"，变成下面的句子：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">should</span> help you.我应该帮你。
                    "Should help"是复合谓语。<br />
                    如果想表达"我可以帮你"，还可以加情态动词"can"，变成下面的句子：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">can</span> help you.我可以帮你。
                    "Can help"是复合谓语。</li>
                <li>3. I am happy every day.我每天开心。</li>
                <li style="list-style: none;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这句话没有情态动词，仅仅表达"我每天开心"这个状态。"Am
                    happy"是一个状态型谓语。如果要表达"我必须开心，我一定要开
                    心"，强调"必须"这个意义，就可以加一个情态动词"must",变成下面的句子：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">must</span> am happy every
                    day.<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但是这个句子是错的，因为，情态动词必须跟动词原型。而"Am"不是系动词 be 的原型，系动词 be 的原型，就
                    是"be"，所以正确的句子应该是：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I must be happy every day.我必须每天开心。<br />
                    "must be happy"也是一个复合谓语，或者严格地说，是一个复合的状态型谓语。</li>
                <li>4. I am a good teacher.我是一个好老师。</li>
                <li style="list-style: none;">如果需要表达"我应该是一个好老师"。就可以加个情态动词"should "，变成：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">should</span> be a good
                    teacher. </li>
                <li>5. You are hungry.你饿了。</li>
                <li style="list-style: none;">如果需要表达"你肯定饿了"，强调"肯定"这个意思，就可以加一个情态动词"must",变成：<br />
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;You <span class="xia-hua-xian">must</span> be hungry
                    .你肯定饿了。</li>
                <li>6. I <span class="xia-hua-xian">can</span> earn money. 我能赚钱。</li>
                <li>7. I <span class="xia-hua-xian">can</span> speak english 我会说英语。</li>
                <li>8. I <span class="xia-hua-xian">can</span> be a good teacher 我可以成为一个好老师</li>
                <li>9. You <span class="xia-hua-xian">should</span> be happy 你应该开心。</li>
                <li>10. He <span class="xia-hua-xian">should</span> find a girlfriend. 他应该找个女朋友。</li>
                <li>11. I <span class="xia-hua-xian">must</span> go now /I have to go 我必须走了。</li>
            </ul>
