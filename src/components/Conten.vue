                    <td>我们的</td>
                    <td>他们/她们/它们的</td>
                </tr>
            </table>
            <br />
            <h2 class="biao-ti-1">2、名词性物主代词</h2>
            <table>
                <tr>
                    <td>mine</td>
                    <td>yours</td>
                    <td>hers</td>
                    <td>his</td>
                    <td>its</td>
                    <td>ours</td>
                    <td>theirs</td>
                </tr>
                <tr>
                    <td>我的</td>
                    <td>你的/你们的</td>
                    <td>她的</td>
                    <td>他的</td>
                    <td>它的</td>
                    <td>我们的</td>
                    <td>他们/她们/它们的</td>
                </tr>
            </table>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;实际上，中文里面，"我的、你的、他的、她的、它的、 我们的、 你们的、他们/她们/它们的"都有两层含义的。<br />
                比如："我的"<br />
                1)这是<span class="xia-hua-xian">我的</span>书。(这里的"我的"在中文里是定语，修饰名词"书"。 所以这里的"我的"翻译成英语，就是一个形容词词性物主
                代词：This is <span class="xia-hua-xian">my</span> book.)<br />
                2)你的书比<span class="xia-hua-xian">我的</span>贵。(这里的"我的"在中文里面含义是"我的书"实际上是指"书"，也就是属于名词的意义。所以这里的"我
                的"翻译成英语就是一个名词性物主代词：<br />
                Your book is more expensive than <span class="xia-hua-xian">mine</span>. Mine=my book)
            </p>
            <h2 class="biao-ti-1">3、形容词性物主代词的用法</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词性物主代词的用法很容易掌握，因为它只有一个作用，就是<span
                    class="xia-hua-xian">作定语</span>，而且还是<span class="xia-hua-xian">作前置定语</span>，所以一定要修饰名
                词，放在一个普通名词前面作前置定语。它只有一个作用，用法非常单一，如果一句话里面，没有名词，根本就不会出现形
                容词性物主代词。像："I am happy every day"，这样的句子是不可能出现形容词性物主代词的，因为里面没有名词。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词性物主代词作前置定语，可以修饰可数名词单数和复数，还可以修饰不可数名词。
            </p>
            <ul>
                <li><span class="xia-hua-xian">My</span> house 我的房子</li>
                <li><span class="xia-hua-xian">your</span> house 你的房子</li>
                <li><span class="xia-hua-xian">Her</span> house 她的房子</li>
                <li><span class="xia-hua-xian">his</span> house 他的房子</li>
                <li><span class="xia-hua-xian">Its</span> house 它的房子(有些宠物有房子)</li>
                <li><span class="xia-hua-xian">our</span> house 我们的房子</li>
                <li><span class="xia-hua-xian">their</span> house 他们/她们的房子</li>
                <li><span class="xia-hua-xian">My</span> houses 我的房子(不止一个房子)</li>
                <li><span class="xia-hua-xian">your</span> cars 你的车(不止一辆车)</li>
                <li><span class="xia-hua-xian">Her</span> boyfriend 她的男朋友</li>
                <li><span class="xia-hua-xian">My</span> money 我的钱</li>
                <li><span class="xia-hua-xian">my</span> water 我的水</li>
                <li><span class="xia-hua-xian">Your</span> coffee 你的咖啡</li>
                <li><span class="xia-hua-xian">Their</span> wine 她们/他们的红酒</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意：形容词词性物主代词，作前置定语的时候，中间一般不能加数词和冠词。所以，像"My two
                books"，"My the book"，"My a book"这些都是错的。
            </p>
            <h2 class="biao-ti-1">4、名词性物主代词用法</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;名词性物主代词就相当于一个名词，所以可以作主语，表语，宾语，等。但是只能在前面提到或者避免重复的时候才能
                用，不能乱用。
            </p>
            <ul>
                <li>1. A: my car is very good.我的车很好。
                    B: <span class="xia-hua-xian">mine</span> is better 我的车更好。(名词性物主代词 mine 作主语，避免重复名词 car，所
                    以用 mine 代替 my car)</li>
                <li>2. A:my car is very good 我的车很好。
                    B: I want <span class="xia-hua-xian">yours</span>. 我想要你的车。 (作宾语，yours=your car)</li>
                <li>3. A:Whose is this house?这是谁的房子？
                    B:It is <span class="xia-hua-xian">hers</span>. 是她的。 (作表语，hers= her house)</li>
                <li>4. My computer is better than <span class="xia-hua-xian">yours</span>. 我的电脑比你的好。
                    因为前面已经提到了"computer"，所以为了避免重复，用名词性物主代词"yours"代替
                    "computer."。"My computer is better than your computer."，这样说，也没有什么错，但是英语
                    是最忌重复的。为了避免前面提到的人或物，后面一般用名词性物主代词。</li>
                <li>5. My house is bigger than <span class="xia-hua-xian">yours</span>.我的房子比你的大。</li>
                <li>6. Her cellphone is more expensive than <span class="xia-hua-xian">mine</span>.她的手机比我的贵。</li>
                <li>7. my student is better than <span class="xia-hua-xian">hers</span>.我的学生比她的学生好。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意：名词性物主代词，不能作定语，因为，形容词性物主代词，已经专门作定语了。<br />
                比如：<br />
                我的书= my book，如果说 "mine book" 就错了，因为形容词性物主代词 my 专门作定语修
                饰 book,名词性物主代词 mine 是不能作定语的。
            </p>
        </div>
