            <h1 class="biao-ti">主语</h1>
            <p>
                <span class="ding-yi">主语是一个句子叙述的主体。</span><br />
                能够充当主语的词语有：名词、代词、数词、动词不定式、动名词、句子、其他。
            </p>
            <hr />

            <h2 class="biao-ti-1">1、名词</h2>
            <p>名词就是世间万物的名称。<br />
                我们生活中，所有看得见的物品，比如，电脑(computer)、
                手机(cellphone)、书(book)、房子(house)、钢笔(pen)等等，都是名词，看不见的也是，
                比如：空气(air)、风(wind)、声音(voice)等等。<br />
                凡是名词都可以作主语，请看下面的例子：<br />
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">Nick</span> is a teacher. 尼克是老师。(主要讲 Nick，所以人名 Nick 是主语。)</li>
                <li>2. <span class="xia-hua-xian">Jack</span> is a teacher. 杰克是老师。(人名 Jack 作主语。)</li>
                <li>3. <span class="xia-hua-xian">Lily</span> is a student. 莉莉是学生。(人名 Lily 作主语。)</li>
                <li>4. <span class="xia-hua-xian">Guang Xi</span> is beautiful. 广西是美丽的。(地名 Guang Xi 作主语。)</li>
                <li>5. <span class="xia-hua-xian">The book</span> is expensive. 这本书很贵。(普通名词 book 作主语。)</li>
                <li>6. <span class="xia-hua-xian">English</span> is easy 英语很容易。(专有名词 English 作主语。)</li>
                <li>7. <span class="xia-hua-xian">China</span> is very large 中国很大。(专有名词 China 作主语。)</li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">2、代词</h2>
            <p>代词就是为了避免重复而代替名词的词语。<br />
                代替人的代词就叫人称代词(有些人称代词也可以指代事物)。<br />
                绝大部分的代词都可以作主语。人称代词又分为主格和宾格。主格和宾格都是指同一个意思，只是作用不同。<br />
                人称代词的主格只有：<br />“I(我)、we (我们)、you (你，你们)、she
                (她)、he(他)、it(它)、they (他们，她们，它们)”。<br />
                主格才可以作主语，宾格不可以，请看例句：<br />
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">I</span> am a teacher. 我是老师。(人称代词主格 I 作主语，注意：不能说 me is a teacher)</li>
                <li>2. <span class="xia-hua-xian">She</span> is beautiful 她很漂亮。(人称代词主格 she 作主语，注意：不能说 her is beautiful)
                </li>
                <li>3. <span class="xia-hua-xian">You</span> are rich.你是有钱的。(人称代词主格 you 作主语)</li>
                <li>4. <span class="xia-hua-xian">He</span> is very handsome.他很帅。(人称代词主格 he 作主语)</li>
                <li>5. <span class="xia-hua-xian">It</span> is cheap.它便宜。(人称代词主格 It 作主语)</li>
                <li>6. <span class="xia-hua-xian">We</span> are busy.我们忙。(人称代词主格 we 作主语)</li>
                <li>7. <span class="xia-hua-xian">They</span> are happy.他们是快乐的。(人称代词主格 they 作主语)<br />
                    This 和 that 是指示代词，也可以作主语。比如：</li>
                <li>1)<span class="xia-hua-xian">This</span> is my pen. 这是我的钢笔。(指示代词 this 作主语)</li>
                <li>2)<span class="xia-hua-xian">That</span> is her computer.那是她的电脑。(指示代词 that 作主语)</li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">3、数词</h2>
            <p>数词就是 1 2 3 4 5 6 7 8 9(基数词)或者第一、第二、第三、第四、第五(序数词)。<br />
                不管是基数词还是序数词都可以作主语，比如：<br />
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">The first</span> is important. 第一很重要。</li>
                <li>2. <span class="xia-hua-xian">Eight</span> is a lucky number.8 是一个幸运号码。</li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">4、动词不定式</h2>
            <p>在讲动词不定式和动名词可以作主语之前，首先了解什么是非谓语动词。<br />
                动词不定式和动名词都叫非谓语动词。<br />
                在英语里面，一个动词原型是不能直接作主语的。比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"eat"是一个动词原型。吃很重要(民以食为天)，不能直接说，"Eat is important"，而要说，"Eating
                is important"或<br />
                者"To eat is important"。"To eat"就是动词不定式，"eating"就是动名词。"Eat,to eat,eating"意思都是一样的，都是"吃"<br />
                的意思，只是形式不一样，因为它们作用不同。"Eat"是谓语动词，作谓语的；而"to eat,eating"不能作谓语，但是可以作
                主语、表语、宾语、 状语等，所以叫非谓语。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在英语里面，当一个动作作主语的时候，
                这个表示动作的动词就要变成非谓语形式(动词不定式或者动名词)才可以作主语，
                这个就是英语的形式变化。<br />
                能作主语的情况：<br />
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">To learn english</span> is easy(作主语)学英语很容易。</li>
                <li>2. <span class="xia-hua-xian">To eat an apple everyday</span> is good for health.(作主语)每天吃一个苹果有益健康。
                </li>
                <li>3. <span class="xia-hua-xian">To be a teacher</span> is hard.(作主语) 做老师是难的。</li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">5、动名词</h2>
            <ul>
                <li>learning English 学英语</li>
                <li>helping poor people 帮助穷人</li>
                <li>being a teacher 成为老师/做老师</li>
            </ul>
            <p>
                作主语：<br />
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">Learning English</span> is funny.学英语是有趣的。(作主语)</li>
                <li>2. <span class="xia-hua-xian">Helping poor people</span> is our duty 帮助穷人是我们的责任(作主语)</li>
                <li>3. <span class="xia-hua-xian">Being a teacher</span> is hard. 做老师是难的(作主语)</li>
            </ul>
            <hr />
            <h2 class="biao-ti-1">6、句子</h2>
            <p>一个句子也可以作主语。作主语的句子我们就叫主语从句。这个内容比较难，以后讲</p>
            <hr />

            <h2 class="biao-ti-1">7、其他</h2>
            <p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在英语里面，有些词语看起来不是名词，但是具有名词的含义。比如，“old”本来是形容词，
                但是加上“the”之后就变成名词的含义了，“the old”表示“老人”的意思，所以可以作主语。
                类此的词语还有：<br />the old 老人 the poor 穷人 the rich 富人 如：</p>
            <ul>
                <li>1. <span class="xia-hua-xian">The rich</span> should help the poor.富人应该帮助穷人。(the rich 作主语)</li>
                <li>2. <span class="xia-hua-xian">“eat”</span> is a verb. “eat”是一个动词。 (“Eat”是动词原型，但是这里加上了引号，表达名词
                    含义，所以也可以作主语。)</li>
            </ul>
            <hr />
            <hr />

            <h1 class="biao-ti-1">特别注意</h1>
            <p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在英语里面，能作主语的只有上面说的 7 个内容，其他一律不可以作主语。<br />
                比如：谓语动词、形容词、介词、副词等都不可以作主语，用这些词语作主语都是错的。
            </p>
        </div>
