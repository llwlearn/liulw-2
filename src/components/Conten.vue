            <h1 class="biao-ti">谓语</h1>
            <p>
                <span class="ding-yi">谓语是说明主语动作或者状态的词语。</span><br />
                谓语分两种，一种是“动作型的谓语”，另一种是“状态型的谓语”。<br />
                说明主语做什么的，就是动作型的谓语；说明主语是什么或者怎么样的就是状态型的谓语。
            </p>
            <hr />

            <h2 class="biao-ti-1">1、动作型的谓语</h2>
            <p>
                动作型的谓语就是由动词构成的。<br />
                动词的定义就是：表示动作或者心理行为的词语。
            </p>
            <h3>
                (1). 简单谓语(由“实义动词/动词短语”构成)
            </h3>
            <p>
                实义动词，就是表示“有实际意义”的动词，用来区别系动词，助动词，情态动词。<br />
                动词短语就是由动词和另一个单词或者几个单词构成的词组。<br />
                <span class="xia-hua-xian">一个实义动词</span>或者<span class="xia-hua-xian">一个动词短语</span>都可以构成简单的谓语。<br>
                常见的简单动词和动词短语：<br />
                say (说)、talk(谈话)、speak(说)、type(打字)、eat(吃)、drink(喝)、
                play(玩)、buy(买)、look at(看)、look after(照顾)、get up(起床)<br />
                以上任何一个实义动词或动词短语都可以作谓语，比如：
            </p>
            <ul>
                <li>1. I <span class="xia-hua-xian">work</span> every day 我每天工作。
                    根据谓语定义：“work”就是说明主语“I”做的动作，所以动词“work”就是谓语</li>
                <li>2. I <span class="xia-hua-xian">run</span> every day 我每天跑步。
                    根据谓语定义：“run”就是说明主语“I”做的动作，所以动词“run”就是谓语</li>
                <li>3. I <span class="xia-hua-xian">love</span> you. 我爱你。
                    根据谓语定义：“Love”就是说明主语“I”做的动作(心理行为)，所以动词“love”就是谓语</li>
                <li>4. I <span class="xia-hua-xian">speak</span> Chinese. 我讲中文。
                    根据谓语定义：“Speak”就是说明主语“I ”做的动作,所以动词“speak ”就是谓语</li>
                <li>5. She <span class="xia-hua-xian">looks after</span> her brother. 她照顾她的弟弟。(主语是第三人单数，所以谓语用了第三人
                    称单数)
                    根据谓语定义：“Looks after”就是说明主语“she”做的动作，所以动词短语“looks after”就是
                    谓语</li>
                <li>6. I <span class="xia-hua-xian">get up</span> at six. 我六点起床。
                    根据谓语定义：“Get up”就是说明主语“I”做的动作，所以动词短语“get up”就是谓语</li>
            </ul>
            <h3>(2). 复合谓语</h3>
            <p>
                复合谓语就是由简单谓语再加上其他词语，共同作谓语的情况。主要有下面三种形式：
            </p>
            <p>
                (1)情态动词+<span class="xia-hua-xian">实义动词</span>或者<span class="xia-hua-xian">动词短语</span>的原形
            </p>
            <ul>
                <li>①I <span class="xia-hua-xian">can speak</span> Cantonese/Mandarin. 我会说广东话/普通话.</li>
                <li>②I <span class="xia-hua-xian">can look after</span> her 我可以照顾她。</li>
            </ul>
            <p>
                (2)助动词+<span class="xia-hua-xian">实义动词</span>或者<span class="xia-hua-xian">动词短语</span>的原形
            </p>
            <ul>
                <li>①I <span class="xia-hua-xian">will buy</span> a new book tomorrow. 我明天要买一本新书。 (一般将来时态)</li>
                <li>②I <span class="xia-hua-xian">will get up</span> early tomorrow.我明天要早起。(一般将来时态)
                    “will buy”和“will get up”都是复合谓语。</li>
            </ul>
            <p>
                (3)助动词+其他的动词形式(be+ doing 或 Have/has+ done)
            </p>
            <ul>
                <li>①I <span class="xia-hua-xian">am working</span> now.我现在正在工作。(现在进行时态) </li>
                <li>②I <span class="xia-hua-xian">have learned</span> English for 10 years.我已经学了 10 年英语了。(现在完成时态)</li>
            </ul>
            <p>
                (2)、(3)都涉及到助动词。助动词是没有意思的，只是用来帮助构成某种结构(所以也叫结构助词)，<br />
                比如：时态，被动语态，疑问句，否定句等。以后学到时态，被动语态，疑问句，否定句的时候，自然就明白了。
            </p>
            <hr />

            <h2 class="biao-ti-1">2、状态型的谓语</h2>
            <p>
                状态型的谓语由“系动词+表语”构成。从某种意义上讲，“系动词+表语”也是一种复合谓语。<br />
                系动词主要由“be”充当，当然其他的系动词也可以。但是那些都是少数的，90%以上的情况是由“be”充当，
                所以经常说“be+表语”。<br />
                “Be”是一个非常重要，也非常特殊的词语。<br />
                “Be”有八种形式：“be 、am、is、 are 、was、 were、 being 、been”，但是它们意思都相同。<br />
                “Be”一般表示“是”的意思，有时候在中文里面不用翻译出来。<br />
                我们最熟悉的“be”的格式就是：“is are am.”<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当主语是“I”就用“am”；当主语是单数的时候用“is”；
                当主语是复数或者“you”的时候用“are”；下面就用“is are am”来举例说明<span class="xia-hua-xian">状态型谓语</span>的情况。比如：
            </p>
            <ul>
                <li>1. I <span class="xia-hua-xian">am happy</span>. 我高兴/我快乐。
                    am 是系动词，happy 是形容词作表语，所以状态型谓语就是“am happy”</li>
                <li>2. He <span class="xia-hua-xian">is a doctor</span> 他是一名医生。
                    is 是系动词，a doctor 是名词作表语，所以状态型谓语就是“is a doctor”</li>
                <li>3. You <span class="xia-hua-xian">are cool</span> 你好酷。
                    are 是系动词，cool 是形容词作表语，所以状态型谓语就是“are cool”</li>
                <li>4. They <span class="xia-hua-xian">are students</span>. 他们是学生。
                    are 是系动词，Students 是名词作表语。所以状态型谓语就是“are students”</li>
            </ul>
            <p>
                另外，“be”的其他格式，也简单了解一下：<br />
                was /were 用于过去时态<br />
                been 用于完成时态<br />
                being 用于被动语态，放介词后面作介词宾语或者构成动名词<br />
                be 用于动词不定式或者放在情态动词，助动词后，如：to be happy, will be, should be,can be<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;状态型的谓语除了系动词外，还有一个重要的内容——表语。
