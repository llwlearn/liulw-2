            <h1 class="biao-ti">系动词</h1>
            <p>
                <span class="ding-yi">系动词也叫连系动词。</span><br />
                定义不重要，因为系动词只有一个作用，就是跟表语构成"系表结构"<span class="xia-hua-xian">作状态型谓语</span>。<br />
                系动词是不能单独用的，必须跟表语一起用。<br />
                反过来说，表语也是不能单独用的。这点前面已经有提到了。<br /><br />
                最常用的系动词就是"be","be"有八种形式。分别是：
            </p>
            <ul>
                <li>1.原型"be"用于助动词和情态动词后面，或者用在动词不定式里面。如：she will be a nurse next month</li>
                <li>2.第一人称 I 后面的"am" 如：I am a teacher.我是老师。注意，只有在一般现在时态里面 I 后面才能用"am."</li>
                <li>3.第三人称单数格式"is" 如：she is a student.她是学生。</li>
                <li>4.You 和复数后面的"are" 如：you are great. 你很伟大。</li>
                <li>5.单数的过去式格式"was"如：he was a boss before.他以前是老板。</li>
                <li>6.复数的过去式格式"were"如：they were farmers before 他们以前是农民。</li>
                <li>7.过去分词格式"been"如： I have been a teacher for three years 我做老师三年了。(用于完成时态)</li>
                <li>8.现在分词格式"being" 如: a bridge is being built.一条桥正在建设中。(被动语态)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"be"不管是哪一种格式，都是表示同一个意思。一般情况，意思相当于中文的"是"，有时候"be"的意思不用翻译出来，
                比如后面加形容词或介词短语作表语的时候。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了"be"，还有少数的实义动词可以充当系动词。这些系动词虽然是从实义动词借用过来的，但是作系动词的意思和作实义动词的意思是不同的。比如，"look"
                作实义动词的时候是"看"的意思，表示一种动作，一般要变成动词短语"look
                at"。作系动词的时候，"look"变成了"看起来"的意思，一般在后面加形容词作表语。如：you look beautiful.(你看起来很漂亮)。<br /><br />
                借过来的系动词，有三个特点：
            </p>
            <ul>
                <li>1 这些系动词，是从实义动词里面借过来的。</li>
                <li>2 借过来作系动词之后，意思马上发生变化。</li>
                <li>3 一般来说，这些借过来的系动词，必须跟形容词作表语。所以通常可以看后面是否跟形容词来判断属于系动词还是实义动词。</li>
            </ul>
            <p>
                因此，上面的例句"You look beautiful."属于<span class="xia-hua-xian">主系表结构</span>。<br />
                You (主语)+Look(借过来的系动词)+Beautiful(形容词作表语)<br /><br />
                这些能充当系动词的实义动词有以下两类：
            </p>
