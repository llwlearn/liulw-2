            <h1 class="biao-ti">一般现在时态的时间状语</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 一般现在时态的时间状语都是表示经常性，习惯性或者有规律性词语。比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;every day 每天 &nbsp;&nbsp;every week 每周 &nbsp;&nbsp;on Sunday
                在周日 &nbsp;&nbsp;in the morning 在早上。
            </p>
            <ul>
                <li>1. She gets up very late <span class="xia-hua-xian">on Sunday</span>. 在周日，她很晚才起床。</li>
                <li>2. I get up at 9:00 <span class="xia-hua-xian">every morning</span>. 我每天早上九点起床。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;首先，"Every+表示时间的名词单数"，可以直接充当一般现在时态的时间状语。<br />
                表示时间的名词分别是：
            </p>
            <table>
                <tr>
                    <td>second 秒</td>
                    <td>minute 分钟</td>
                    <td>hour 小时</td>
                    <td>morming 早上</td>
                </tr>
                <tr>
                    <td>noon 中午</td>
                    <td>afernoon 下午</td>
                    <td>evening 傍晚</td>
                    <td>night 夜晚</td>
                </tr>
                <tr>
                    <td>day 天 lyear 年</td>
                    <td>week 周</td>
                    <td>month 月</td>
                    <td>season 季度</td>
