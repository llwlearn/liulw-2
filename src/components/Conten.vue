            <h1 class="biao-ti">比较级和最高级的用法</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词的比较级和最高级跟形容词的原级一样，<span
                    class="xia-hua-xian">可以作前置定语和表语，甚至宾补</span>。<br />
                比如：
            </p>
            <ul>
                <li>I have a better way 我有一个更好的办法(形容词比较级 better 作前置定语)</li>
                <li>She is my best friend 她是我最好的朋友 (形容词最高级 best 作前置定语)</li>
                <li>She made me the happiest man in the world 她让我成为世界上最幸福的男人(形容词最高级
                    happiest 作前置定语)</li>
                <li>Eating apples makes us healthier 吃苹果让我们更健康。(形容词比较级 healthier 作宾补)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当然，形容词的比较级和最高级作表语是最常见的，以下是<span
                    class="xia-hua-xian">形容词比较级和最高级作表语的情况</span>：
            </p>
            <h3 class="biao-ti-1">1、A 超过 B 当表示 A 事物(人)比 B 事物(人)强的时候<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;用下面的句型：A +be+ 形容词的比较级+than+ B (than B 是比较状语)</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="xia-hua-xian">句型里面的形容词一定要变成比较级的格式</span>，than
                是一个连接词，中文"比"的意思，than
                跟后面的词语一起构成比较状语：
            </p>
            <ul>
                <li>1. I am taller than he/him. 我比他高。(than 后面的人称代词可以是主格也可以是宾格)
                    (主语+系动词+表语+比较状语)</li>
                <li>2. She is fatter than me. 她比我胖。</li>
                <li>3. My girlfriend is more beautiful than her.我女朋友比她漂亮。</li>
                <li>4. My book is cheaper than yours 我的书比你的便宜。(Yours=your book)</li>
                <li>5. My boss is richer than yours.我的老板比你的老板有钱。</li>
                <li>6. My daughter is more beautiful than hers.我女儿比她女儿漂亮。</li>
            </ul>
            <h3 class="biao-ti-1">2、A 不如 B 当表示 A 事物(人)不如 B 事物(人)强的时候<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;用下面的句型：A +be not+形容词的比较级+than+ B</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这个句型很容易，直接在<span class="xia-hua-xian">上面的句型里面的"be"后面加否定词
                    not</span> 就可以了：
            </p>
            <ul>
                <li>1. I am not taller than he 我没有他高。(他比我高)</li>
                <li>2. she is not fatter than me 她没有我胖。(我比她胖)</li>
                <li>3. My girlfriend is not more beautiful than her 我女朋友没有她漂亮。(她比我女朋友漂亮)</li>
            </ul>
            <h3 class="biao-ti-1">3、A 和 B 一样 当表达表示 A 事物(人)和 B 事物(人)一样的时候<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;用下面的句型：A + be +as+形容词的原级+as+ B</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意，这里的形容词<span
                    class="xia-hua-xian">一定要用形容词的原级</span>。"As….as"表示"和。。。一样"的意思。
            </p>
            <ul>
                <li>1. I am as tall as him 我和他一样高。</li>
                <li>2. She is as fat as me 她和我一样胖。</li>
                <li>3. My girlfriend is as beautiful as her 我女朋友和她一样漂亮。</li>
            </ul>
            <h3 class="biao-ti-1">4、A 是最什么的……当表示在一定范围内，A 事物(人)是最厉害的<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;用下面的句型：A+be+the +形容词的最高级+表示范围的词语</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这里的形容词<span class="xia-hua-xian">一定要用最高级</span>，并且前面一定要<span
                    class="xia-hua-xian">加定冠词"the"</span>，这个也是定冠词的一种用法。因为"人外有人，天外
                有天"，一个事物(人)只能在一定范围内是最强的，所以后面还要加一个表示范围的词语，一般是介词短语构成。
            </p>
            <ul>
                <li>1. He is the richest in Guangxi.他在广西是最有钱的</li>
                <li>2. The house is the highest in Guangzhou.这个房子在广州是最高的。</li>
                <li>3. I am the tallest in the class. 我是班上最高的。</li>
                <li>4. She is the fattest in our class.她是我们班上最胖的。</li>
                <li>5. My girlfriend is the most beautiful in my heart.我女朋友在我心里是最漂亮的。</li>
                <li>6. He is the most famous in China.在中国，他是最出名的。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意：虽然形容词比较级和最高级作表语，前置定语的情况也比较常见。但是<span
                    class="xia-hua-xian">最常见的还是形容词的原级作表语</span>，<span class="xia-hua-xian">前置
                    定语</span>，甚至<span class="xia-hua-xian">宾补</span>。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，<span class="xia-hua-xian">形容词也可以放在不定代词后面作定语</span>，<span
                    class="xia-hua-xian">修饰或限制不定代词</span>。如：<br />
                something new/interesting/special&nbsp;&nbsp;&nbsp;&nbsp; 新的/有趣的/特别的一些事
            </p>
        </div>
