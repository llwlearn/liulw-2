            <h1 class="biao-ti">助动词</h1>
            <p>
                <span class="ding-yi">助动词是没有意思的，它的作用就是帮助实义动词或系动词构成复合谓语，或者构成疑问句，否定句，以及构成各种时态，语态和语气。</span><br />
                具体的用法，在讲述时态和语态的时候会详细介绍。这里简单了解一下就可以了。<br />
                常用的助动词有： be do have will would shall should<br />
                注意，be(包括其他形式)、 do(did does)、 have (has,had) 是从系动词和实义动词那里借过来的，<span
                    class="xia-hua-xian">充当助动词的时候是没有意思的</span>。<br /><br />
                助动词有三个特点:
            </p>
            <ul>
                <li>1. 没有意思。</li>
                <li>2. 不能单独用，必须帮助构成其他各种结构。</li>
                <li>3. 没有专门的助动词，都是从其他动词借过来的。</li>
            </ul>
            <p>
                下面通过具体的例子来学习助动词：
            </p>
            <ul>
                <li>1. I <span class="xia-hua-xian">am</span> working now. 我现在正在工作。</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(这是一个现在进行时态的句子。这里的"am"属于助动词，不是系动词。如果是系动词，是完全不一样的，首先后面
                    必须跟表语，比如，形容词，名词，介词短语等等，第二，作系动词的时候，"am"是有意思的。在这里，"am"是完
                    全没有意思，主要的作用是跟动词的现在分词构成现在进行时态。<br />
                    "am"符合助动词的特点：1 没有意思，2 帮助构成现在进行时态，3 从系动词借过来用的。)</li>
                <li>2. <span class="xia-hua-xian">Do</span> you love me? 你爱我吗？</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(这里的"do"没有意思，帮助构成一般疑问句，是一个从实义动词借过来的助动词。)</li>
                <li>3. I <span class="xia-hua-xian">do</span> not love you.我不爱你。("do"没有意思，帮助构成一般现在时态否定句。)</li>
                <li>4. I <span class="xia-hua-xian">did</span> not love you.我以前不爱你。("did"没有意思，帮助构成一般过去时态否定句。</li>
                <li>5. She <span class="xia-hua-xian">does</span> not love you. 她不爱你。("does"没有意思，帮助构成一般现在时态另一种否定句。)</li>
                <li>6. I <span class="xia-hua-xian">have</span> learned English for ten years.我已经学英语十年了。</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(这里的"have"没有意思，跟动词的过去分词"learned"一起构成现在完成时态。是一个从实义动词借过来的助动词。)
                </li>
                <li>7. She <span class="xia-hua-xian">has</span> known you for ten years.她已经认识你十年了。</li>
                <li style="list-style: none;">
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(这里的"has"没有意思，跟动词的过去分词"known"一起构成现在完成时态。)</li>
                <li>8. <span class="xia-hua-xian">Does</span> she love me? 她爱我吗？("does"没有意思，帮助构成一般现在时态一般疑问句。)</li>
                <li>9. I <span class="xia-hua-xian">have</span> eaten dinner. 我已经吃过饭了。("have"没有意思， 帮助构成现在完成时态。)</li>
                <li>10. I <span class="xia-hua-xian">will</span> buy ten glasses tomorrow. 我明天要买 10
                    个杯子。("will"没有意思。帮助构成一般将来时态。)</li>
                <li>11. I <span class="xia-hua-xian">do</span> love her. 我确实(真的)爱她。 ( "Do"在肯定句里起强调语气，没有真正意义。)</li>
            </ul>
