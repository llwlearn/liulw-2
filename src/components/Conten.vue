            <h4 class="biao-ti-1">一般现在时态的用法</h4>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;首先，我们要知道，什么时候需要使用一般现在时态的规则造句。当表示经常性，习惯性，有规律性的动作和状态的时
                候需要使用一般现在时态。表示<span class="xia-hua-xian">真理性，客观性的动作和状态</span>也要使用一般现在时态。(如，地球是圆的，太阳从东边升起，1+1=2)。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，还有两种情况，也要使用一般现在时态。第一种就是：不轻易改变的状态。什么叫不轻易改变的状态？比如：
                "他是一个男孩"，是男的，不会轻易变成女的。再比如："这个房子很大"，房子不会轻易变小的。像这样的句子都要使用一
                般现在时态。(He is a boy.The house is very big.)第二种就是： 此时此刻的状态。比如："我现在很累"( I am tired now)。
            </p>
