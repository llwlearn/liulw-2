            <h1 class="biao-ti">可数名词不规则变化</h1>
            <p>
                <span class="ding-yi">不规则的变化是少数的。</span><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有一种误区，就是不规则的变化要死记硬背。这些不规则的变化，死记是没有什么效果的，因为如果不去运用这些名词<br />
                的复数，是很难记住的，就算暂时记住了，过几天也会忘记。所以最有效的方法，就是用这些<span class="xia-hua-xian">名词的复数造句</span>。<br>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Man- men 男人&nbsp;&nbsp; mouse- mice 老鼠&nbsp;&nbsp; goose-
                geese 鹅&nbsp;&nbsp; ox- oxen 牛&nbsp;&nbsp; child-children 小孩<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有些可数名词，<span class="xia-hua-xian">单复数都是相同</span>：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Deer 鹿&nbsp;&nbsp; sheep 绵羊&nbsp;&nbsp; Chinese
                中国人&nbsp;&nbsp; Japanese 日本人 <br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vietnamese 越南人&nbsp;&nbsp; Lebanese 黎巴嫩人&nbsp;&nbsp;
                Portuguese 葡萄牙人<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有些可数名词，只有复数的形式，没有单数的形式，称为<span
                    class="xia-hua-xian">复数名词</span>，如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;People 人们&nbsp;&nbsp; police 警察&nbsp;&nbsp; public
                公众&nbsp;&nbsp; clothes 衣服(总称)&nbsp;&nbsp; cattle 牛(总称)
            </p>
