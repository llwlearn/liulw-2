            <h1 class="biao-ti">名词所有格</h1>
            <p>
                <span class="ding-yi">名词所有格是表示所属关系的一种格式。</span><br />
                比如：小明的书包。"小明的" 就是所有格。因为书包是属于小明的。再比如，妈妈的手。"妈妈的"就是名词所有格。<br />
                英文的名词所有格有两种构成方式：
            </p>
            <hr />

            <h2 class="biao-ti-1">1、有生命的名词+ 's</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这里的"'s"就相当于中文"妈妈的手"里面的"的"(Mother's hand)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意：这里的名词是表示有生命的事物，一般是指人和动物。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这种名词所有格，构成方式很容易，直接在一个表示人或动物的名词后面加"'s"就可以了，表示人的名词，一般是人名
                或表示身份，职业的名词。如果是人名，直接加"'s"就可以构成一个名词所有格。但是，如果是一个普通的可数名词，变成
                所有格的时候，要注意"可数名词单数不能单独使用"这个规则。比如：
            </p>
            <ul>
                <li>Nick's 尼克的</li>
                <li>Jack's 杰克的</li>
                <li>Lily's 莉莉的</li>
                <li>Lucy's 露西的</li>
                <li>My friend's 我朋友的(friend 是可数名词单数，不能单独用，加了限定词 my，才可以构成
                    名词所有格)</li>
                <li>The doctor's 这个医生的(doctor 是可数名词单数，不能单独用，加了 the，才可以构成名词
                    所有格)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="xia-hua-xian">名词所有格一般作前置定语，修饰名词</span>。比如：<br />
                <span class="xia-hua-xian">Nick's</span> book 尼克的书 <span class="xia-hua-xian">Mother's</span> kiss
                妈妈的吻<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;名词所有格作前置定语的时候可以<span
                    class="xia-hua-xian">修饰可数名词</span>，也可以<span class="xia-hua-xian">修饰不可数名词</span>。如：
            </p>
            <ul>
                <li><span class="xia-hua-xian">Lily's</span> tea 莉莉的茶 (修饰不可数名词)</li>
                <li><span class="xia-hua-xian">Mother's</span> hands 妈妈的手(修饰可数名词)</li>
                <li><span class="xia-hua-xian">my sister's</span> friends 我姐姐的朋友(修饰可数名词)</li>
                <li><span class="xia-hua-xian">The dog's</span> food 狗的食物(狗粮)(修饰不可数名词)</li>
                <li><span class="xia-hua-xian">the dog's</span> tail 狗的尾巴 (修饰可数名词)</li>
                <li><span class="xia-hua-xian">the bird's</span> nest 这只鸟的窝/鸟巢(修饰可数名词)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果修饰可数名词，可以修饰单数，也可以修饰复数：<br />
                可以说：Nick's <span class="xia-hua-xian">book</span> 也可以说：Nick's <span
                    class="xia-hua-xian">books</span><br />
                但是，不可以说：Nick's three books。如果想表示"Nick 的三本书"，应该说：three books of Nick's，这个叫span class="xia-hua-xian">双重所有
                格。这里暂时不详细讲，后面的课程专门讲。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果名词是"s"结尾的复数或名词本身是"s"结尾的，直接加"'"就可以了，当然也可以加"'s"<br />
                如：the boss' car/the boss's car 这个老板的车 teachers' day 教师节<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;虽然说，由"'s "构成的名词所有格，必须是有生命的名词。但也有些例外的情况，比如
                下面的例子：<br />
                today's newspaper 今天的报纸<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由"'s "构成的名词所有格除了可以作前置定语，还可以<span
                    class="xia-hua-xian">作表语</span>：
            </p>
            <ul>
                <li>1. A:&nbsp;&nbsp;Whose pen is it？这是谁的钢笔 B:&nbsp;&nbsp;It is <span class="xia-hua-xian">Tim's</span>.
                    是提姆的</li>
                <li>2. A:&nbsp;&nbsp;Whose cellphone is it? 这是谁的手机 B:&nbsp;&nbsp;It is <span
                        class="xia-hua-xian">Nick's</span>. 是尼克的</li>
                <li>3. The house is <span class="xia-hua-xian">my father's</span>.这个房子是我爸爸的。</li>
            </ul>
            <hr />

            <h2 class="biao-ti-1">2、无生命的名词用"of +无生命名词"</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我们先来看，这样的短语：<span class="xia-hua-xian">这个城市的中心</span><br />
                这里的名词所有格是"这个城市的"，<span class="xia-hua-xian">城市</span>是没有生命的事物，英语是不可以说：the city's center.在英语里面，要用"of +名
                词" 的结构表达。应该说：the center of the city. 这里"of the city" 才是正确的名词所有格。这种名词所有格比较难，因
                为作定语的时候，需要放在被修饰的名词后面，也就是作<span class="xia-hua-xian">后置定语</span>。这里的"of "是"的"意思，相当于前面的"'s"，而且<br />
                "of+无生命名词"构成的名词所有格也是表示所属关系，但是因为作后置定语，所以表示"前面的名词属于后面的名词"。
                比如"The center of the city"的"The center"属于"the city."<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这种名词所有格的结构是：<span
                    class="xia-hua-xian">of+一个没有生命的名词</span>。但是，一定要注意，如果这个名词是一个可数名词，也要<span class="xia-hua-xian">考虑单
                    复数问题</span>或<span class="xia-hua-xian">冠词问题</span>。比如：
            </p>
            <ul>
                <li>Of the house 这个房子的</li>
                <li>Of houses 房子的</li>
                <li>Of my house 我房子的</li>
                <li>Of this coat 这件大衣的</li>
                <li>Of my coat 我的大衣的</li>
                <li>Of the carrot 这个胡萝卜的</li>
                <li>(上面的名词：house,coat,carrot 都是可数名词，不能直接用，所以都加了一些限定词或冠词
                    或变复数，才能构成所有格。)</li>
                <li>Of water 水的</li>
                <li>Of oil 油的</li>
                <li>(上面的名词 water 和 oil 是不可数名词，所以可以直接用。当然，也可以根据情况加上某个定语)</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，被这种所有格修饰的名词，不管单复数，可数或不可数名词，都必须<span
                    class="xia-hua-xian">加定冠词"the"</span>，这也是定冠词的一种规
                则。冠词也是一个难点，所以这种名词所有格，对初学者来说，有点难。不管多难都可以掌握的，多做点练习就可以了。<br />
                请看下面的例子：
            </p>
            <ul>
                <li>1. The price of the house 这房子的价格(名词所有格 of the house 作后置定语修饰名词 price,
                    所以 price 要加冠词 the)</li>
                <li>2. The color of the house 这房子的颜色</li>
                <li>3. The color of the carrot 这个胡萝卜的颜色</li>
                <li>4. The price of oil 油的价格</li>
                <li>5. The gate of our school 我们学校的大门</li>
                <li>6. The covers of the book 这本书的封面</li>
                <li>7. The roof of the house 这个房子的顶部(房顶)</li>
                <li>8. The answers of the exam 这试卷的答案</li>
            </ul>
            <p>
                例外：
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;today's newspaper/
                the newspaper of today 今天的报纸<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Shanghai's
                industry/the industry of Shanghai 上海的工业<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The school's
                history/the history of the school 学校的历史<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这种没有生命的名词所有格作后置定语的时候，还要特别注意一个问题：所属关系。也就是这种名词所有格拿去<span
                    class="xia-hua-xian">修饰名
                    词作后置定语的时候，必须考虑所属关系</span>。如果没有所属关系，不能乱作后置定语，因为会产生逻辑问题。比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The water of the cup
                错误<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The water in the cup
                正确<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; "杯子的水"，其实水是并不属于杯子的，杯子不能拥有水。中文的"杯子的水"，实际上是指"杯子里面的水"，所以应该
                用介词短语"in the cup"作后置定语才是正确的。
            </p>
