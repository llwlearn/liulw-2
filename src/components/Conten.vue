            <h1 class="biao-ti">指示代词</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指示代词也很容易掌握。只有两个，"this"和"that"，还有它们的复数："these"和"those"
            </p>
            <table style="width: 800px; margin-top: -2px">
                <tr>
                    <td>单数</td>
                    <td>复数</td>
                </tr>
                <tr>
                    <td>This 这个(作主语 be 动词用 is)</td>
                    <td>These 这些(作主语 be 动词用 are)</td>
                </tr>
                <tr>
                    <td>That 那个(作主语 be 动词用 is)</td>
                    <td>Those 那些(作主语 be 动词用 are)</td>
                </tr>
            </table>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指示代词可以作：主语、宾语、表语、定语。其中作主语和定语最常见。
            </p>
            <h2 class="biao-ti-1">1、作定语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"<span class="xia-hua-xian">This</span>"和"<span
                    class="xia-hua-xian">that</span>"作前置定语，专门修饰或限定单数的可数名词，不能修饰复数，不过可以
                修饰不可数名词。比如：
            </p>
            <ul>
                <li><span class="xia-hua-xian">This</span> house 这个房子</li>
                <li><span class="xia-hua-xian">This</span> computer 这台电脑</li>
                <li><span class="xia-hua-xian">This</span> girl 这个女孩</li>
                <li><span class="xia-hua-xian">That</span> boy 那个男孩</li>
                <li><span class="xia-hua-xian">That</span> company 那个公司</li>
                <li><span class="xia-hua-xian">That</span> student 那个学生</li>
                <li><span class="xia-hua-xian">This/that</span> student is very tall.这/那个学生很高。</li>
                <li><span class="xia-hua-xian">This</span> computer is cheap.这台电脑很便宜。</li>
                <li>I bought <span class="xia-hua-xian">this</span> computer.我买了这台电脑。</li>
                <li><span class="xia-hua-xian">This</span> water 这水，这些水</li>
                <li><span class="xia-hua-xian">This</span> money 这钱，这些钱</li>
                <li><span class="xia-hua-xian">That</span> money 那钱，那些钱</li>
                <li>This houses 错误(this 不能修饰复数)</li>
                <li>That houses 错误(that 不能修饰复数)</li>
                <li><span class="xia-hua-xian">this</span> book 这本书</li>
                <li><span class="xia-hua-xian">that</span> book 那本书</li>
                <li>"<span class="xia-hua-xian">These</span>"，"<span
                        class="xia-hua-xian">those</span>"也可以作前置定语，修饰或限定可数名词复数，只能修饰可数名词复数，
                    <span class="xia-hua-xian">不能修饰单数，也不能修饰不可数名词</span>。比如：
                </li>
                <li><span class="xia-hua-xian">These</span> houses 这些房子</li>
                <li><span class="xia-hua-xian">Those</span> computers 那些电脑</li>
                <li><span class="xia-hua-xian">These</span> girls 这些女孩子</li>
                <li><span class="xia-hua-xian">Those</span> boys 那些男孩子</li>
                <li>These girl 错(these 不能修饰单数)</li>
                <li>Those boy 错(those 不能修饰单数)</li>
                <li><span class="xia-hua-xian">These</span> Girls are beautiful.这些女孩很漂亮。</li>
                <li>I support <span class="xia-hua-xian">these</span> boys.我支持这些男孩。</li>
                <li><span class="xia-hua-xian">these</span> books 这些书。</li>
                <li><span class="xia-hua-xian">those</span> books 那些书。</li>
            </ul>
            <h2 class="biao-ti-1">2、作主语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"<span class="xia-hua-xian">That</span>","<span
                    class="xia-hua-xian">this</span>" 作主语的时候是单数概念，如果是主系表结构，"be"动词用单数格式"is"或"was"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"<span class="xia-hua-xian">These</span>","<span
                    class="xia-hua-xian">those</span>"作主语的时候是复数概念，如果是主系表结构，"Be"要用复数格式"are"或"were"。<br />
                比如：
            </p>
            <ul>
                <li>1. <span class="xia-hua-xian">This</span> is my book. 这是我的书。</li>
                <li>2. <span class="xia-hua-xian">Those</span> are her pencils. 那些是她的铅笔。</li>
                <li>3. <span class="xia-hua-xian">That</span> is my student.那是我的学生。</li>
                <li>4. <span class="xia-hua-xian">This</span> is my wife. 这是我妻子。</li>
                <li>5. <span class="xia-hua-xian">These</span> are my students.这些是我的学生。</li>
                <li>6. <span class="xia-hua-xian">Those</span> are my friends.那些是我的朋友。</li>
                <li>7. <span class="xia-hua-xian">Those</span> are my books.那些是我的书。</li>
            </ul>
            <h2 class="biao-ti-1">3、作宾语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指示代词可以作宾语，但是使用情况很少，这里就不详细解释。
            </p>
            <ul>
                <li>1. I want <span class="xia-hua-xian">this</span>. 我要这个。 </li>
                <li>2. I like <span class="xia-hua-xian">that</span>. 我喜欢那个。</li>
                <li>3. I will buy <span class="xia-hua-xian">these/those</span>. 我要买这些/那些。</li>
            </ul>
            <h2 class="biao-ti-1">4、作表语</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;指示代词可以作表语，使用情况更少了。<br />
                It is <span class="xia-hua-xian">this</span>. 就是这个。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;很多人，受中文影响，通常犯这样一个代词错误："she brother","I mother","you
                father."因为在中文的口语里面，我
                们经常说，"她哥"，"我妈"，"你爸"。我们很少说："她的哥哥"，"我的妈妈"，"你的爸爸"。所以有些人，就直接说，"she
                brother","I mother","you father"，这是很典型的错误，因为"she，I，you"是人称代词的主格，是不能作定语的，不能修
                饰名词(人称代词宾格也是不能作定语的，比如 "me mother"也是错的)。作定语，修饰名词，应该用形容词性物主代
                词："Her brother","my mother","your father"。
            </p>
