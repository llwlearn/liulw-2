            <h1 class="biao-ti">形容词的定义</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词[adjective 简称 adj.或 a.] ，是英语很常见的词类。形容词主要用来<span
                    class="ding-yi">修饰名词或代词</span>，表示人或事物的性质，状
                态，和特征的程度好坏与否。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;形容词是一个很重要的词语，使用频率高，数量也多（<span
                    class="xia-hua-xian">排在数词和名词的后面</span>），所以要多积累形容词。形容词分为两
                种：<br />
            </p>
            <h2 class="biao-ti-1">1.普通形容词</h2>
            <p>
                beautiful 漂亮的&nbsp;&nbsp;&nbsp;&nbsp; big 大的&nbsp;&nbsp;&nbsp;&nbsp; green 绿色的&nbsp;&nbsp;&nbsp;&nbsp;
                happy 快乐的<br />
                long 长的&nbsp;&nbsp;&nbsp;&nbsp; cheap 便宜的&nbsp;&nbsp;&nbsp;&nbsp; angry 生气的&nbsp;&nbsp;&nbsp;&nbsp; sad
                伤心的<br />
            </p>
            <h2 class="biao-ti-1">2.复合形容词</h2>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;复合形容词是中文没有的概念。复合形容词就是<span
                    class="xia-hua-xian">由两个单词构成的一个新的单词</span>，这个新词也属于形容词的词性。两个
                单词之间有一个符号“-”，请看下面的词语：<br />
                sky-blue 天蓝色的&nbsp;&nbsp;&nbsp;&nbsp; good-looking 好看的&nbsp;&nbsp;&nbsp;&nbsp; man-made 人造的<br />
                second-hand 二手的&nbsp;&nbsp;&nbsp;&nbsp; warm-hearted 热心的&nbsp;&nbsp;&nbsp;&nbsp; hard-working 努力的，勤勉的
            </p>
