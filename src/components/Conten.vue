            <h1 class="biao-ti">可数名词单数变复数</h1>
            <span class="ding-yi">可数名词单数变复数的规律：</span>
            <p>
                1. <span class="xia-hua-xian">大部分可数名词变复数，在词尾+s</span>.如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;pen - pens 钢笔&nbsp;&nbsp; cup-cups 杯子&nbsp;&nbsp; book-books
                书&nbsp;&nbsp; computer-computers 电脑
            </p>
            <hr />

            <p>
                2. <span class="xia-hua-xian">当可数名词是以字母 s 、sh 、x 、ch 结尾的单词，变复数要+es</span>. 如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;bus-buses 汽车&nbsp;&nbsp; box-boxes 箱子&nbsp;&nbsp; kiss-kisses
                吻&nbsp;&nbsp; watch-watches 手表<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;brush-brushes 刷子&nbsp;&nbsp; toothbrush-toothbrushes 牙刷<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有一个例外的单词：stomach-stomachs 胃，复数形式直接加 s。
            </p>
            <hr />

            <p>
                3. <span class="xia-hua-xian">辅音字母+y 结尾的名词，去 y 变 i+es</span>.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;英语字母里，除了元音字母"A、U、I、O、E"外，其他都是辅音字母。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;当一个可数名词是由"辅音字母+y"结尾的，那么变复数的时候，就要把这个"y"变成"i"，再加"es"，比如："city"
                这个<br />
                单词，"t"是辅音字母，后面还有一个"y"，所以"city"的复数就是"cities"。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;baby-babies 婴儿 country-countries 国家 Story- Stories 故事
                Dictionary - Dictionaries 字典<br />
                例外：如果是元音字母加 y 结尾的单词，就不用变化了，直接加 s 变复数。如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;toy --- toys 玩具&nbsp;&nbsp; boy----boys 男孩&nbsp;&nbsp;
                day----- days 日子、天
            </p>
            <hr />

            <p>
                4. <span class="xia-hua-xian">当可数名词是以字母 f 或 fe 结尾的单词，去 f 或 fe 变 v 再+es</span>.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Wife-wives 妻子&nbsp;&nbsp; knife-knives 小刀&nbsp;&nbsp;
                thief-thieves 小偷&nbsp;&nbsp; leaf-leaves 叶子&nbsp;&nbsp; Wolf–wolves 狼<br />
                例外：roof-roofs 房顶&nbsp;&nbsp; gulf-gulfs 海湾&nbsp;&nbsp; cliff-cliffs 悬崖
            </p>
            <hr />

            <p>
                5. <span class="xia-hua-xian">辅音字母+o 结尾的可数名词变复数也是+es</span>.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;tomato-tomatoes 西红柿&nbsp;&nbsp; potato-potatoes 土豆&nbsp;&nbsp;
                hero-heroes 英雄&nbsp;&nbsp; sweetpotato –sweetpotatoes 红薯
                这类词语很少 ，常见的就是上面几个，可以通过下面的句子来记：
                Heroes like tomatoes and potatoes.(英雄喜欢西红柿和土豆)
            </p>
            <hr />

            <p>
                <span class="xia-hua-xian">例外：</span><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;很多"元音字母+o"结尾的可数名词或者以"o"结尾的外来词、简写词都是直接加"s",如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;bamboo-bamboos 竹子&nbsp;&nbsp; piano-pianos 钢琴&nbsp;&nbsp;
                radio-radios 收音机 <br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;photo-photos 相片&nbsp;&nbsp; video-videos 录像，视频&nbsp;&nbsp;
                studio-studios 工作室，画室，播音室
            </p>
