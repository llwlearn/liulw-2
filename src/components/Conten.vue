                Every night 每晚 &nbsp;&nbsp;every evening 每天傍晚 &nbsp;&nbsp;every minute 每一分钟<br />
                Every day 每天 &nbsp;&nbsp;Every week 每周 &nbsp;&nbsp;Every month 每月 &nbsp;&nbsp;Every year 每年
            </p>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上短语可以直接放在句子后面作一般现在时态的时间状语：
            </p>
            <ul>
                <li>1. I miss you <span class="xia-hua-xian">every second</span></li>
                <li>2. I teach English <span class="xia-hua-xian">every night</span></li>
                <li>3. She is busy <span class="xia-hua-xian">every day</span></li>
                <li>4. We are happy <span class="xia-hua-xian">every day</span></li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，一般现在时态的时间状语也可以由频度副词充当。下面是常用的频度副词：
            </p>
            <table>
                <tr>
                    <td>Always (总是)</td>
                    <td>oten (经常)</td>
                    <td>usually (通常)</td>
                    <td>sometimes (有时候)</td>
                </tr>
                <tr>
                    <td>seldom (很少)</td>
                    <td>occasionally (偶尔)</td>
                    <td>never (从不)</td>
                    <td></td>
                </tr>
            </table>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不过这些频度副词作时间状语的时候不是放在句尾，而是放在中间，并且有一定的规则。<br />
                请看下面的规则：
            </p>
            <p>
                频度副词的位置<br />
                1,放在 be,助动词,情态动词后面
            </p>
            <ul>
                <li>I am <span class="xia-hua-xian">often</span> late.我经常迟到。注意：I often am late 是错的</li>
                <li>She is <span class="xia-hua-xian">always</span> hard-working.她总是很努力。</li>
                <li>Do you <span class="xia-hua-xian">always</span> get up late? 你总是起得很晚吗？</li>
                <li>Can you <span class="xia-hua-xian">often</span> read English?你可以经常朗读英语吗？</li>
                <li>I can <span class="xia-hua-xian">often</span> run in the morning.我可以经常在早上跑步。</li>
            </ul>
            <p>
                2,放在实义动词前面
                即：主语+(频度副词)+ 谓语 +宾语
            </p>
            <ul>
                <li>I <span class="xia-hua-xian">never</span> drink wine</li>
                <li>I <span class="xia-hua-xian">often</span> play basketball.我经常打篮球。</li>
                <li>She <span class="xia-hua-xian">always</span> comes back late at night.晚上，她总是很晚回来。</li>
                <li style="list-style: none">(注意，不要因为频度副词"always"在前面，就把"comes"变回原型"come"。这是错的，没有这样的规则。只有助动
                    词 do/does/did 和情态动词后面的动词才会变原型)</li>
                <li>I <span class="xia-hua-xian">seldom</span> go shopping .我很少去购物。</li>
                <li>I <span class="xia-hua-xian">usually</span> go home after work.下班后，我通常回家。</li>
                <li>I <span class="xia-hua-xian">sometimes</span> watch TV on Sunday.我有时候在周日看电视</li>
                <li>I <span class="xia-hua-xian">sometimes</span> go to see films. 我有时候去看电影。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其实一句话里面，可以跟几个时间状语。但是有时可能逻辑不对，比如：Every day 和 often , always
                连用会出现矛
                盾，比如：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I <span class="xia-hua-xian">always</span> eat bread <span
                    class="xia-hua-xian">every day</span> (语法没有错误，但是有矛盾)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"我总是每天吃面包"中文里面"总是"修饰"每天"，没毛病。但是，英文的状语是修饰谓语的，"Always" 是状
                语，"every day"也是状语，都是修饰谓语"eat" 。这样就出现矛盾了：总是吃，同时又每天都吃，这就有矛盾了，因
                为，"总是吃"不代表"每天都吃"。两者出现的频率不同，"每天都吃"的频率比"总是吃"高。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果改成"I always eat bread in the morning"就对了，为什么呢？因为"In the
                morning"是泛指："经常性的早上"，
                或者，一般情况下表示"在早上这个时间，不一定是每天早上"。也就是，这句话的意思是，"在早上的时候，总是吃面包"，
                这样，就不强调"每次，每一天早上"，这样，频率跟"always"差不多是一样的，这两个状语就不冲突了。
            </p>

