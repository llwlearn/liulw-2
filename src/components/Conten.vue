            <h1 class="biao-ti">定语</h1>
            <p>
                <span class="ding-yi">修饰或限定名词的词语叫定语。</span><br />
                定语只跟名词有关系。哪里有名词，哪里就有定语。<br />
                不管在什么语言里面，名词都是需要定语的(特殊情况除外)。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果没有定语，名词的 范围就太广了。意思就不清楚了。比如说：你去帮我买苹果。这句话是有问题的，因为苹果这个<br />
                名词，没有定语，概念太广了，表示世界上所有的苹果。如果这样跟别人说话，别人根本不理解，让我买多少苹果啊，什么<br />
                苹果啊？所以要加上一个定语：<span class="xia-hua-xian">你去帮我买十个苹果</span>。这样意思就清楚了，"十个"就是修饰名词"苹果"的，所以"十个"就是定语。<br>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意，在英语里面，普通名词才能带定语，一般情况下，专有名词不能带定语的，除非特殊情况。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中文的定语，不管有多少个，或者有多长，都是放在前面的。请看例子：<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="xia-hua-xian">我的</span>书 <span
                    class="xia-hua-xian">美丽善良的</span>姑娘 <span class="xia-hua-xian">昨天晚上我买的</span>书 <span
                    class="xia-hua-xian">我经常建议你买的</span>书<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在英文里面也有放在前面的定语，叫前置定语。另外，还有后置定语，就是放在名词后面修饰前面的名词的定语。中文<br />
                是没有后置定语的概念，所以受中文影响，大部分人都觉得后置定语难。实际上，后置定语本身不难，只是我们不习惯而已。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在英语里面能作前置定语的有：形容词性物主代词、形容词、名词所有格(’s)、数词、名词、量词、指示代词等。例如：
            </p>
            <ul>
                <li>1. 形容词性物主代词作前置定语：<span class="xia-hua-xian">my</span> book 我的书 <span class="xia-hua-xian">her</span>
                    pen 她的钢笔 <span class="xia-hua-xian">our</span> teacher 我们的老师</li>
                <li>2. 形容词作前置定语：<span class="xia-hua-xian">expensive</span> book 贵的书 <span
                        class="xia-hua-xian">good</span> book 好的书 <span class="xia-hua-xian">happy</span> girl 快乐的女孩
                </li>
                <li>3. 名词所有格(‘s)作前置定语： <span class="xia-hua-xian">Nick's</span> house 尼克的房子</li>
                <li>4. 数词作前置定语：<span class="xia-hua-xian">three</span> books 三本书 <span class="xia-hua-xian">Ten</span>
                    apples 十个苹果</li>
                <li>5. 名词作前置定语：<span class="xia-hua-xian">English</span> book 英语书 <span class="xia-hua-xian">room</span>
                    number 房间号码</li>
                <li>6. 量词作前置定语：<span class="xia-hua-xian">a bottle of</span> water 一瓶水 </li>
                <li>7. 指示代词作前置定语：<span class="xia-hua-xian">this</span> book 这本书 <span class="xia-hua-xian">that</span>
                    girl 那个女孩</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意，同一个普通名词，可以跟多个定语。比如：<span class="xia-hua-xian">Two
                    beautiful</span> girls 两个漂亮的女孩后置定语由介词短语、名词所有<br />
                格(of +名词)、动名词短语、动词不定式短语、句子构成。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如：The people <span class="xia-hua-xian">in the park</span> are
                dancing. 在公园里面的人们正在跳舞<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;很多人都理解错了这句话，都翻译成"人们在公园里跳舞"。这个是典型受中文思维影响的情况。在这个句子里，介词<br />
                短语"in the park"是作后置定语，修饰名词"people"的。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但是，为什么会有人翻译成："人们在公园里跳舞"呢？因为中文的状语是放在中间的，"人们在公园里跳舞"这句话里面<br />
                的"在公园里"属于中文的地点状语。所以翻译错的人都是把"<span class="xia-hua-xian">英文的定语</span>"理解成"<span
                    class="xia-hua-xian">中文的状语</span>"了。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"人们在公园里面跳舞"，在英文里面是这样说的：The people are dancing <span
                    class="xia-hua-xian">in the park</span>.英语的地点状语都是放在后面<br />
                的，极少放在中间。当然这个例子容易混淆，看下面的例子就容易理解了：
            </p>
            <ul>
                <li>1. The people <span class="xia-hua-xian">in shanghai</span> are rich 在上海的人都很富有。</li>
                <li> 2. The friends <span class="xia-hua-xian">from Hongkong</span> are studying Mandarin 来自香港的朋友正在学普通话。
                </li>
                <li>3. The girl <span class="xia-hua-xian">with a book</span> is his English teacher.拿着一本书的女孩是他的英文老师。
                </li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外，放在不定代词后面，修饰不定代词的词语也叫定语，也属于后置定语。如：<br />
                something <span class="xia-hua-xian">new</span> 一些新的事情 something <span
                    class="xia-hua-xian">interesting</span> 一些有趣的事情 anything <span class="xia-hua-xian">special</span>
                一些特别的事情。
            </p>
