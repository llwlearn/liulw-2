        <div class="zhu-ti" id="div1_9">
            <h1 class="biao-ti">九个基本句型</h1>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;现在我们已经大概了解各个句子成分的情况了。但是只知道句子成分，还不能造句。还需要知道各个句子成分的位置。<br />
                英语里面有五种摆放句子成分的方式，也就是五个基本句型，或者五个造句的基本公式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;英语的句子不管多么复杂，都是由这个五个基本句型变化而成的。
            </p>
            <h2 class="biao-ti-1">1、主语+谓语(不及物动词) +状语(表示动作)</h2>
            <ul>
                <li>1. I work <span class="xia-hua-xian">every day</span>. 我每天都工作。<br />
                    "Work"是不及物动词作谓语，"every day"是时间状语。</li>
                <li>2. I run every day.我每天跑步。<br />
                    "run"是不及物动词作谓语，"every day"是时间状语。</li>
                <li>3. I live <span class="xia-hua-xian">in Zhongshan city</span>.我住在中山市。<br />
                    "Live"是不及物动词作谓语，"in Zhongshan city"是地点状语。</li>
            </ul>
            <h2 class="biao-ti-1">2、主语 + be(系动词)+ 表语+状语(表示状态)</h2>
            <ul>
                <li>1. She is rich <span class="xia-hua-xian">now</span>. 她现在很富有。<br />
                    "Rich"是形容词作表语，"Now"是时间状语。</li>
                <li>2. I am in Guangdong <span class="xia-hua-xian">now</span>. 我现在在广东。<br />
                    "In Guangdong"是介词短语作表语，"Now"是时间状语。</li>
                <li>3. I was a worker <span class="xia-hua-xian">before</span>. 我以前是一个工人。<br />
                    "Worker"是名词作表语，"Before"是时间状语。</li>
            </ul>
            <h2 class="biao-ti-1">3、主语+谓语(及物动词)+宾语+状语(表示动作)</h2>
            <ul>
                <li>1. I read English <span class="xia-hua-xian">every morning</span>. 我每天早上读英语。<br />
                    "read"是及物动词作谓语，"English"是名词作宾语，"every morning"是时间状语。</li>
                <li>2. We bake beef every month.我们每个月烤牛肉。<br />
                    "bake"是及物动词作谓语，"beef"是名词作宾语，"every month"是时间状语。</li>
                <li>3. She learns English very <span class="xia-hua-xian">hard</span>. 她很努力学英语。<br />
                    "learn"是及物动词作谓语，"English"是名词作宾语，"hard"是方式副词作状语。</li>
            </ul>
            <h2 class="biao-ti-1">4、主语 +谓语(及物动词里面的双宾动词)+双宾语+状语(表示动作)</h2>
            <ul>
                <li>1. I gave her a book <span class="xia-hua-xian">yesterday</span>. 我昨天给了她一本书。<br />
                    "gave"及物动词作谓语，"her"是间接宾语，"book"是直接宾语，"Yesterday"是时间状语。</li>
                <li>2. They sent me a computer <span class="xia-hua-xian">last week</span>.上周，他们送我一台电脑。<br />
                    "sent"是及物动词作谓语，"me"是间接宾语，"computer"是直接宾语，"last week"是时间状语。</li>
            </ul>
            <h2 class="biao-ti-1">5、主语 + 谓语(少数特定的及物动词)+宾语+宾补+状语(表示动作)</h2>
            <ul>
                <li>1. I made her happy <span class="xia-hua-xian">yesterday</span>. 昨天，我让她开心。<br />
                    "made"是及物动词作谓语，"her"作宾语，"happy"是形容词作宾补，"yesterday"是时间状语。</li>
                <li>2. Her father called her Baby Bear <span class="xia-hua-xian">before</span>.她爸爸以前叫她"宝贝小熊"。<br />
                    "Called"及物动词作谓语，"her"作宾语，"Baby Bear"是名词作宾补，"before"是时间状语。</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;通过上面的例子我们知道，其实英语可以分为两个基本句型。第 1，3，4，5 个句型都有一个共同点，都是表示动作；<br />
                第 2 个句型表示状态，所以我把英语的句型划分为两个基本句型。分别是：<br />
                1.状态型的：主语 +(系)be + 表语+状语<br />
                2.动作型的：主语 +谓语+ 宾语+状语 (这个句型也代表其他三个，即第 1， 4 ，5 句型)<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;这样划分的目的是把语法简单化，让大家更容易理解。另外也是为方便大家造句，因为，
                所有的语言其实都可以分为动作的句子和状态的句子。我们说的每一句话都可以分为动作和
                状态的，比如：<br />
            </p>
            <ul>
                <li>1. 今天天气很好=状态的句子</li>
                <li>2. 我每天都很开心=状态的句子</li>
                <li>3. 我每天都学英语=动作的句子</li>
                <li>4. 她经常买衣服=动作的句子</li>
            </ul>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;我们学语法就是为了造句。那么当我们造一个句子的时候，首先要做的事情就是弄清楚，我们要造的句子是动作的还是<br />
                状态的，然后选择一个句型。如果你要表达状态的句子，就选择：主语 +(系)be + 表语+状语。比如上面的句子：我每天都很开心。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因为是状态的句子，我们就可以这样说：I am happy every day.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;再看这个句子：我每天都学英语。因为是动作的句子，所以英文这样说：I learn English every
                day.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;注意了，我们造英文的句子，首先要做的是选择正确的句型。之后才是时态，词语等等。这是第一步，这一步错了，后<br />
                面的都错。很多人说这样的句子：I am like English.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本来要表达，"我喜欢英语"的意思。但这个是一个动作的句子，动作的句子是没有 be 动词"am"
                的，所以这句话是错<br />的，正确的应该是：I like English.<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;想要尽快说一口流利的英语，写出地道的英语文章，就要养成这样的习惯，先分清动作和状态的句子。我们学英语语<br />法，是为了造句和正确理解英语的句子。很多人走入了误区和极端，专门拿语法知识去分析句子,这样就偏离了学语法的真正目的了。
            </p>
        </div>
