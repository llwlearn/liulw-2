            </p>
            <h4 class="biao-ti-1">(2)由"普通名词+专有名词"或"专有名词+普通名词"构成的专有名词前带 the</h4>
            <ul>
                <li><span class="xia-hua-xian">the</span> New York Times《纽约时报》</li>
                <li><span class="xia-hua-xian">the</span> People's Republic of China 中华人民共和国</li>
            </ul>
            <p>
                New York 是专有名词，times 是普通名词，符合"专有名词+普通名词"，所以"New York Times"
                前面加冠词"the"
            </p>
            <h4 class="biao-ti-1">(3)报纸.书籍.杂志的专有名词</h4>
            <ul>
                <li><span class="xia-hua-xian">the</span> Times 《泰晤士报》</li>
                <li><span class="xia-hua-xian">the</span> Bible《圣经》</li>
                <li><span class="xia-hua-xian">the</span> Reader's Digest《读者文摘》</li>
            </ul>
            <h4 class="biao-ti-1">(4)团体机构,学校,医院,公共建筑的名称前</h4>
            <ul>
                <li><span class="xia-hua-xian">the</span> United Nations 联合国</li>
                <li><span class="xia-hua-xian">the</span> Red Star Hospital 红星医院</li>
