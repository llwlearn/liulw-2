            <h1 class="biao-ti">实义动词的五种形式</h1>
            <p>
                <span class="ding-yi">任何一个实义动词都有五种形式，也可以理解成五种写法或者五种格式。</span><br />
                这五种格式的名称分别是：<span class="xia-hua-xian">原形</span>，<span class="xia-hua-xian">过去式</span>，<span
