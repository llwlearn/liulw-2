            <h1 class="biao-ti">谓语动词和非谓语动词</h1>
            <p>
                <span class="ding-yi">动词就是表示动作或者心理行为的词语。</span><br />
                动词从用法来分，可以分为<span class="xia-hua-xian">谓语动词</span>和<span class="xia-hua-xian">非谓语动词</span>。<br />
                它们的区别就是一个作谓语，另一个不能作谓语(作主语，表语，宾语等)<br />
                但是，不管是谓语动词还是非谓语动词，意思都是一样的。<br />
                非谓语是一个比较难，也很重要的内容，后面会专门讲述，这里主要讲谓语动词。<br />
                谓语动词有四种：<span class="xia-hua-xian">实义动词</span>，<span class="xia-hua-xian">系动词</span>，<span
                    class="xia-hua-xian">助动词</span>和<span class="xia-hua-xian">情态动词</span>。
            </p>
        </div>
        <!--5_2-->
        <div class="zhu-ti" id="div5_2">
            <h1 class="biao-ti">实义动词(及物动词和不及物动词)</h1>
            <p>
                <span class="ding-yi">实义动词，有些语法书也称为行为动词。实义动词，顾名思义，就是有实际意义的动词。</span><br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;大部分动词都是有实际意义。但是也有少数动词是没有意义的，或者意义不完整的，比如系动词，助动词和情态动词。
                所以也可以这样理解实义动词：除了系动词，助动词和情态动词，剩下的就是实义动词了。<br />
                实义动词又分为<span class="xia-hua-xian">及物动词</span>和<span class="xia-hua-xian">不及物动词</span>。<br />
            </p>
            <h3 class="biao-ti-1">及物动词</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就是<span
                    class="xia-hua-xian">后面一定要加宾语的动词</span>，也就是加一个动作的对象或者承受者。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;及物动词如果不加宾语，句子的意思就不完整了。<br />
                比如："买(buy)" 这个动词，我们造句的时候，不可能只说："我买"，或者"你买"。如果你去买东西的时候，跟售货员说：
                "小姐，我买"，不说买什么东西，她肯定认为你是神经病。"买"这动词，后面必须要加一个动作的对象，买什么东西啊。
                "买书"还是"买钢笔"，"书"和"钢笔"就是动作的对象。如果你说："我买书"，意思就完整了，所以"买(buy)"就是一个典型的及物动词。
            </p>
            <h3 class="biao-ti-1">不及物动词</h3>
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;就是<span
                    class="xia-hua-xian">后面不需要加宾语的动词</span>。而且也不能加宾语，因为意思已经完整了，不需要宾语了。<br />
                比如："work"，"Work" 是"工作"的意思。"I work every day"(我每天工作)，意思已经完整了。你不能说"工作你，工作
                他"，这样说逻辑有问题。什么叫"工作你，工作他"？所以不能加宾语，加了也是错的。再比如："I live Beijing."这个也是
                错的，因为"live"是不及物动词，不能跟宾语"Beijing"。不及物动词后面可以跟状语，所以"我在北京生活"应该是："I live
                in Beijing."这里是"in Beijing"是地点状语。符合五个基本句型第一个：<span class="xia-hua-xian">主语+不及物动词+状语</span>。
            </p>
            <hr />
            <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不及物动词的数量不多，<span
                    class="xia-hua-xian">大部分实义动词是及物动词</span>。也有很多实义动词既可以作及物动词也可以作不及物动词。很多
                人觉得这点很难，其实不是很难，因为一个实义动词作及物动词和作不及物动词意思通常不一样，也就是说，通过意思去区分。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;比如："run "这个动词既可以作及物动词也可以作不及物动词。作不及物动词的时候表示"跑步"，后面没有宾语。例
                如："I run every morning."(我每天跑步)。当它作及物动词的时候，表示"经营，管理"的意思，例如："I run a website."(我经营一个网站)。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;还有很多这样的动词，这里不详细解释，大家以后慢慢积累。另外，尽量通过字典去了解，字典里面都有详细的说明。
            </p>
        </div>
