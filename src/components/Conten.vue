            <h1 class="biao-ti">状语</h1>
            <p>
                <span class="ding-yi">状语就是说明谓语的情况，状语只跟谓语有关系。</span><br />
                英语的状语一般放在句子的后面，这点跟中文不一样，中文的状语放在中间。(如：我<span class="xia-hua-xian">今天</span>很开心。我在<span
                    class="xia-hua-xian">广东</span>教英语)
                状语分为很多种，有时间状语，地点状语，方式状语，原因状语，目的状语等等。<br />
                状语一般由副词，介词短语充当，以后的课程会详细讲解。下面用简单的介词短语和副词来举例理解状语的定义。<br />
                In Guangxi 在广西<br />
                In Beijing 在北京<br />
                这是两个最简单的介词短语，可以作地点状语，比如：
            </p>
            <ul>
                <li>1. I teach English <span class="xia-hua-xian">in Guangxi</span>. 我在广西教英语。<br />
                    ("in Guangxi"是一个简单的介词短语，作地点状语修饰谓语"teach".)</li>
                <li>2. I teach English <span class="xia-hua-xian">in Beijing</span>. 我在北京教英语。<br />
                    ("in Beijing" 是一个简单的介词短语，作地点状语修饰谓语"teach".)</li>
                <li>3. We work <span class="xia-hua-xian">in Guangxi</span>.我们在广西工作。<br />
                    ("in Guangxi" 是一个简单的介词短语，作地点状语修饰谓语"work".)</li>
                <li>4. I am happy <span class="xia-hua-xian">in Guangxi</span>.我在广西开心。<br />
                    ("in Guangxi" 是一个简单的介词短语，作地点状语修饰状态型谓语"am happy"。)</li>
                <li>5. I am busy <span class="xia-hua-xian">in Guangxi</span>.我在广西忙。<br />
                    ("in Guangxi" 是一个简单的介词短语，作地点状语修饰状态型谓语"am busy"。)</li>
                <li>6. I am happy <span class="xia-hua-xian">today</span>. 我今天开心。<br />
                    " today"是时间状语(时间副词作状语)。这里的状语"today(今天)"主要说明谓语"am happy "的情况，"am happy"(状态型的谓语)发生在
                    today。状语"today"跟主语"I"没有关系，因为"I"不可能是"today(今天)"。"I"是"人"不是"今天"。这也说明了，状语只跟谓语有关系。
                </li>
                <li>7. I am busy <span class="xia-hua-xian">now</span>.我现在忙。<br />
                    ("now"是一个简单的时间副词，作时间状语修饰状态型谓语 "am busy"。)</li>
                <li>8. We are learning English <span class="xia-hua-xian">now</span>.我们正在学英语。<br />
                    ("now" 是一个简单的时间副词，作时间状语修饰复合谓语"are learning"。)</li>
                <li>9. I will buy a book <span class="xia-hua-xian">tomorrow</span>.我明天买一本书。<br />
                    ("tomorrow" 是一个简单的时间副词，作时间状语修饰复合谓语 "will buy"。)</li>
                <li>10. I will go home <span class="xia-hua-xian">tomorrow</span>. 我明天回家。<br />
                    (时间副词"Tomorrow" 作时间状语。)</li>
                <li>11. She bought a new computer <span class="xia-hua-xian">yesterday</span>.<br />
                    她昨天买了一台新电脑。(时间副词"yesterday"作时间状语)</li>
                <li>12. I teach English <span class="xia-hua-xian">here</span>.我在这里教英语。<br />
                    ("here"是一个简单的地点副词，作地点状语修饰谓 语"teach"。)</li>
                <li>13. I am happy <span class="xia-hua-xian">here</span>.我在这里开心。<br />
                    ("here" 是一个简单的地点副词，作地点状语修饰状态型 谓语 "am happy"。)</li>
                <li>14. She is watching TV <span class="xia-hua-xian">upstairs</span>.她在楼上看电视。<br />
                    (地点副词"upstairs"作地点状语。)</li>
                <li>15. I speak English <span class="xia-hua-xian">slowly</span>.我说英语慢/我慢慢地说英语。<br />
                    "slowly"就是一个方式副词，作方式状语。这里"slowly"(慢慢地)主要是修饰谓语"speak "，
                    主要讲"说得慢"。而不是主语"I"慢，也不是宾语"English"慢。所以状语就是说明谓语的情况
                    的，只跟谓语有关系。跟主语，宾语没有多大的关系。</li>
                <li>16. She reads English <span class="xia-hua-xian">loudly</span>.她大声地朗读英语。<br />
                    (方式副词"loudly"作方式状语。)</li>
                <li>17. She looked at him <span class="xia-hua-xian">angrily</span>.她生气地看着他。<br />
                    (方式副词"angrily"作方式状语。)</li>
            </ul>
            <p>
                英语还有一种频度副词也可以作状语，不过位置比较特殊，以后会细讲。比如：<br />
                Often 经常 seldom 很少 usually 通常 always 总是 sometimes 有时候<br />
                1.I <span class="xia-hua-xian">usually</span> go home after work.我通常下班后回家。(频度副词"usually"作时间状语。)<br />
                2.He <span class="xia-hua-xian">often</span> plays basketball after
                school.他放学后经常打篮球。(频度副词"often"作时间状语。)<br />
                注意：英语一句话里面可以同时出现多个状语。比如：<br />
                I teach English <span class="xia-hua-xian">in Guangxi now</span>.我目前在广西教英语。(出现两个状语：地点状语"in Guangxi"
                和时间状语"now"。)多个状语的位置规则以后再详细讲。
